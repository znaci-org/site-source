<h1>Radovan Zogović - Iako neće doći</h1>

<p>
  <i>– majkama, udovicama, sestrama crvenoarmejaca koji su pali na našim poljanama –</i>
</p>

Ja znam puti su njini nedohodni i oni nikad neće<br/>
stići na rodna polja, pod trepetne brezove sjene,<br/>
niti će kucnuti u okna, kao da bace proso,<br/>
ni ozariti majke, ni sestre tavne, ni žene,<br/>
ni pogladiti rukom dijete zlatokoso;<br/>
i neće sjesti na traktor, što drhti pred prvim skokom,<br/>
niti će s nožicama zaći pod mokro jabukovo drveće,<br/>
ni dati mašini ulja –<br/>
ko ljekar kapi u oko.<br/>
<br/>
Nikada neće stići… Al oni u mojoj domovini žive:<br/>
žive u onima koje su prošla ista tanad ubojita,<br/>
u djeci koja na rađanju dobiju njihova imena;<br/>
u božurima žive, u krizantemama, u šumi perunika,<br/>
i obnavljaju se po zakonu godišnjih doba i mijena,<br/>
besmrtnošću trava, biljaka, cvjetova i žita.<br/>
Ko ruzmarin rastu, cvjetaju ko kadive –<br/>
ko žarke bulke –<br/>
uz mermer spomenika!<br/>
<br/>
Nikada neće stići. Al, pavši – pali su licem<br/>
k zemlji, da krv joj dadu iz rane ravno u rane,<br/>
da joj imena šanu, da šapnu zavjetne poklike.<br/>
I žive:<br/>
zavjete njihove šapuću nadgrobne trake,<br/>
imena vaša šapću lahoru bijele latice,<br/>
njihove poklike zbore svirale od zovike;<br/>
i trube dječačke, od kore s vrbove grane,<br/>
trubeći proljeću našem,<br/>
trube njihove znake!<br/>
<br/>
Ja znam: puti su njini nedohodni i oni neće doći.<br/>
Ali, vi u srcu znajte – za bankom, u tkačkom stanku,<br/>
u grkoj tuzi nad knjigom koju su čitali,<br/>
i nad klasalim poljem, i kuća kad pucka u noći,<br/>
i kad pjevate sinu pjesmu uspavanku –<br/>
u srcu znajte:<br/>
po zemlji na kojoj su pali<br/>
oni govore i žive – iako neće doći…<br/>
