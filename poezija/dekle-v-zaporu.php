<br/>
Moj dragi fant,<br/>
ne veš, kaj vse počeli so z menoj ...<br/>
Kako lepo bilo bi iti za teboj,<br/>
v dolenjskih hribih pasti kakor ti ...<br/>
<br/>
Umrla bi ...<br/>
A jaz živim, živim<br/>
in naj rodim otroka njim,<br/>
ki naju so ubili,<br/>
otroka njim,<br/>
ki materam otroke so morili?<br/>
<br/>
Moj dragi fant,<br/>
tako mi je, ko da bi kdo čez mé oral;<br/>
če zdaj bi me pogledal,<br/>
me nebi več spoznal.<br/>
A tebe otrok bo poznal,<br/>
poznal ne bo bandita,<br/>
in vedel bo, zakaj si pal,<br/>
zakaj je tvoja kri prelita.<br/>
