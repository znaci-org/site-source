Zaš’ne javljaš da l’je, roblje živo?“<br>
<br>
„Roblje živo, al ne mož’ da dođe,<br>
Dubok Dunav pa ne mož’ da prođe.<br>
<br>
Vetar duva pa mi lađu ljulja. <br>
Da sam vila pa da imam krila,<br>
Dosada bi u Nemačko bila,<br>
Pred lager bi bombu opustila,<br>
Pa bi prva vrata otvorila,<br>
I onda bi roblje ispustila. <br>
Svaka majka ruke bi širila,<br>
Da zagrli svoga milog sina,<br>
Svaka sestra za brata rođena,<br>
Svaka ljuba za svoga dragana...<br>
<br>
A Đermanci vrzani u lanci, <br>
A Đermanke vrzane u lance. <br>

<p>(Dragutin M. Đorđević: Život i običaji narodni u Leskovačkoj Moravi, Naučno delo, Beograd, 1958., str. 653.)</p>