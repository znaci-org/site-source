<h2>II verzija</h2>

Vjetar duva gora se prolama<br>
Proleter se s puškom razgovara.<br>
<br>
Puško moja od čelika čista<br>
Ti ćeš tući prokletog fašista.<br>
<br>
Puško moja mali karabine,<br>
Hoću li te nositi do zime.<br>
<br>
Puško moja od čelika sjajna,<br>
U mnogoj si pjesmi opjevana.<br>

<h2>III verzija</h2>

<pre>
Vjetar duva, gora se prolama,  
proleter se s puškom razgovara:  

Puško moja od čelika čista,  
ne promaši prokletog fašista.  

Puško moja, mali karabine,  
hoću li te nositi do zime?  

Puško moja od čelika sjajna,  
mnoga te je proklinjala nana.
</pre>

<p>(<i>zapisao Mirko Raković</i>)</p>

