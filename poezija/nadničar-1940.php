<h1>Miroslav Mika Antić - Nadničar 1940</h1>

Mene su počele od devete da tuku<br/>
debele gazde, — banatski zlatni buđ,<br/>
i kad god šaptah, svi su se drali: „Umukni!"<br/>
na svojoj zemlji, jesam li svima tuđ?<br/>
<br/>
Do jeseni je dosta da crkneš kraj brazde.<br/>
Žudim k'o niko, vršidbe u Banatu,<br/>
jer sve kad prođe, dobiću jankel od gazde,<br/>
i metar žita, — prvu đuturašku platu.<br/>
<br/>
Reci mi, ti, što čuvaš ovce na ledini,<br/>
je si li ikad u mekoj postelji spav'o?<br/>
Prostirka noći, — jorgan sirotinjski jedini.<br/>
Snop suncokreta, — jastuk pod umornom glavom.<br/>
<br/>
— Il' možda sve to gledamo suviše detinje?<br/>
— A zar je život kad te levčama tuku?<br/>
Nebo je meni: ponjava, — a ne svetinja.<br/>
Vriskaću divlje, ma da svi da psuju: „Umukni!"<br/>
<br/>
Sinoć sam seo na rogalj među starije,<br/>
i videh: patnja u mozgu im se vrti.<br/>
<br/>
Ako je život uvek ovakva svinjarija,<br/>
moraću radost da izmislim pre smrti.<br/>
