<h1>Oton Župančič - Veš, poet, svoj dolg?</h1>

Veš, poet, svoj dolg!<br/>
Nimaš nič besed?<br/>
Kaj zagrinjaš se v molk!<br/>
Vrzi pesem v svet,<br/>
pesem za današnjo rabo;<br/>
vsi jo bomo povzeli za tabo.<br/>
<br/>
Vem, o vem svoj dolg,<br/>
v prsih tu me žge;<br/>
naj se mi grlo odpre,<br/>
bom zavijal ko volk,<br/>
na vse štirih bom stal vrh planine,<br/>
škripal in hlipal med pečine.<br/>
<br/>
Vrh Možaklje bom stal,<br/>
pesem svojo rjul,<br/>
Blegaš jo bo čul,<br/>
Krim odmev bo dal;<br/>
v vse vetrove jo bom tresel,<br/>
eden jo bo na Pohorje nesel ...<br/>
<br/>
Gozd in gora poj,<br/>
silen ženimo hrup;<br/>
boga gmajna, le vkup<br/>
le vkup, le vkup z menoj<br/>
staro pravdo v mrak tulimo,<br/>
da se pretúlimo skozi to zimo.<br/>
<br/>
Še bo kdaj pomlad,<br/>
še bo napočil zor;<br/>
tákrat volčji zbor<br/>
pojde lovce klat:<br/>
plani čez Savo, plavaj čez Dravo<br/>
zob za zob in glavo za glavo!<br/>

<p>1941</p>

<h1>Pesniče, znaš svoj dug?</h1>

Pesniče, znaš li svoj dug?<br/>
Zar ćutiš u ovaj čas?<br/>
Zašto se kriješ u muk?<br/>
Pesmu daj za nas!<br/>
Pesmu, današnjim nadahnutu dobom:<br/>
složno ćemo je prihvatit za tobom.<br/>
<br/>
znam, o znam svoj dug,<br/>
U grudima me peče.<br/>
<br/>
Čim glas mi poteče<br/>
zaurlaću ko vuk,<br/>
četvoronoške staću vrh planina,<br/>
i zarežaću sred pećina.<br/>
<br/>
S vrha Možaklje tad<br/>
zaurlaću je mlad,<br/>
Blegaš će pesmu čuti,<br/>
a Krim će odjeknuti;<br/>
U sve ću vetrove da je utresem,<br/>
da u Pohorje jedan je snese.<br/>
<br/>
Planino, šumo, poj!<br/>
Grmnimo uglas svi!<br/>
Rajo, pevaj i ti!<br/>
<br/>
Na okup, rode moj!<br/>
Na staru pravdu podsetimo,<br/>
da zimu ovu prebrodimo.<br/>
<br/>
Proleća opet će cvati,<br/>
zore će opet sjati<br/>
a gladni vučji čopor<br/>
lovačka grla će klati.<br/>
Preleti Savu, preplivaj Dravu —<br/>
zub za zub i glava za gljavu.<br/>

<p>(preveo sa slovenačkog Petar Vujačić)</p>
