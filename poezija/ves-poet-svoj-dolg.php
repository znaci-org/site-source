<h1>Pesniče, znaš li svoj dug? <small>(prevod Petar Vujačić)</small></h1>

Pesniče, znaš li svoj dug?<br/>
Zar ćutiš u ovaj čas?<br/>
Zašto se kriješ u muk?<br/>
Pesmu daj za nas!<br/>
Pesmu, današnjim nadahnutu dobom:<br/>
složno ćemo je prihvatit za tobom.<br/>
<br/>
znam, o znam svoj dug,<br/>
U grudima me peče.<br/>
<br/>
Čim glas mi poteče<br/>
zaurlaću ko vuk,<br/>
četvoronoške staću vrh planina,<br/>
i zarežaću sred pećina.<br/>
<br/>
S vrha Možaklje tad<br/>
zaurlaću je mlad,<br/>
Blegaš će pesmu čuti,<br/>
a Krim će odjeknuti;<br/>
U sve ću vetrove da je utresem,<br/>
da u Pohorje jedan je snese.<br/>
<br/>
Planino, šumo, poj!<br/>
Grmnimo uglas svi!<br/>
Rajo, pevaj i ti!<br/>
<br/>
Na okup, rode moj!<br/>
Na staru pravdu podsetimo,<br/>
da zimu ovu prebrodimo.<br/>
<br/>
Proleća opet će cvati,<br/>
zore će opet sjati<br/>
a gladni vučji čopor<br/>
lovačka grla će klati.<br/>
Preleti Savu, preplivaj Dravu —<br/>
zub za zub i glava za gljavu.<br/>

<h1>Oton Župančič - Pesniče, dužnost znaš? <small>(prevod Desanka Maksimović)</small></h1>

Pesniče, dužnost znaš?<br/>
Zar nemaš nijednu reč?<br/>
Što si utonuo u muk?<br/>
Backi pesmu u svet,<br/>
nadahnutu današnjim dobom,<br/>
mi ćemo je pevati za tobom.<br/>
<br/>
