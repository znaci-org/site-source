<h1>August Cesarec - Čežnja za mirom u sebi samome</h1>

Bijesno me razvlači olujni krvavi kovitlac života<br/>
i ja sebe nemam – o, kako čeznem za mirom!<br/>
Osjećam da bih ljudima bio veći prijatelj,<br/>
da ih mogu posmatrati kroz prizmu kristalne tišine.<br/>
Ljudi se trgaju za kost slobode i sreće.<br/>
Vlast im je glavni oltar u hramu oskvrnavljenom krvlju,<br/>
a meni je svaka vlast mrska,<br/>
i slobodu i sreću vidim tek na groblju sviju briga.<br/>
O, biti od svih briga slobodan,<br/>
hodati kroz život sa čarobnim štapićem tišine,<br/>
na sve krize gledati s vrha kojeg su nevidljive katastrofe,<br/>
imati savršenstvo smirenja u duši,<br/>
biti šetalac kroz život,<br/>
imati sebe –<br/>
zar će to uvijek biti tek čežnja za vječnim zapjevkom<br/>
umornog: nikad?<br/>
U vječnoj čežnji za mirom na vrhu najviših saznanja,<br/>
ja ću i umrijeti onako kako sam živio,<br/>
protjeran gost samoga sebe,<br/>
razdrt olujnim krvavim kovitlacem života,<br/>
koji sam i sam uzvitlao i sam sam bio njegova –<br/>
svoja vlastita žrtva.<br/>

<p>(iz zbirke “Stihovi”, Zagreb, 1919.)</p>
