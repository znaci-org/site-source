<h2>II verzija</h2>

<pre>
Slavna je puška, garav drugar,
Slavna je rogulja, slavan andžar,
Al’ od sviju sam slavnija ja,
Ja skromna torba seljačka.
O puško moja, o torbo ti,
Vi ćete bitku dobiti!

Bilo je nekad tužnih dana,
Kad smo morali preko okeana
U kletu Ameriku, daljine maglene,
Gdje naša mladost u rudniku vene.
O, davni dani prošli u tuđini,
O, torbo druže vjerni i jedini,
Danas kad dani dođoše slave
I torba krenu na pute krvave,
I pjeva na njoj sitan šaren vez
Kada se javi naš puškomitraljez.
O puško moja, o torbo ti,
Vi ćete bitku dobiti!

Komoro bojna, znadeš li ti,
Tebe će torba nadbiti,
Hvala i slava do neba njoj,
Šarenoj torbi seljačkoj.
Sad kad su došli najteži dani,
Ona nam vojsku slobode hrani,
U ratu, u miru, sred dobra i zla,
Torba nas svuda pratila.
O puško moja, o torbo ti,
Vi ćete bitku dobiti!

Ide na front na leđima junaka,
Katkad je nosi i stara baka,
Il’ sjedi čiča, il’ brižna žena
Ili pak njina djeca malena.
Hrabar partizan daleko je čuje
Kad torba na front k njemu putuje.
O puško moja, o torbo ti,
Vi ćete bitku dobiti!

Fašistička gamad, šta misle oni,
Hranu im dovlače voz i kamioni,
Al’ uzalud sve to dovlači on —
Jača je torba nego kamion.
Uz tutanj topova kad počne borba
I voz oklopni prevpne torba.
O puško moja, o torbo ti,
Vi ćete bitku dobiti!
</pre>

<p>(Savo Orović, ratni dnevnik 1941-1945 (str. 289), Beograd 1972.)</p>

<hr>

<p>O svom doživljaju seljačke torbe na Kozari pripovijedao je legendarni partizanski komandant dr Mladen Stojanović pjesniku Branku Ćopiću, nukajući ga da o tome napiše pjesmu. Tako je nastala jedna od omiljenih, popularnih partizanskih pjesama, ujedno prva Ćopićeva pjesma partizanka, kojom je započeo svoje bogato stvaralaštvo.</p>
