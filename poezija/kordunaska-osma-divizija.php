<h2>Pjesma Osmoj kordunaškoj diviziji</h2>

Pjesma ori, daleko se čuje<br/>
Kuda Osma udarna putuje.<br/>
Sipa narod po četama cvijeće,<br/>
To se, druže, zaboravit’ neće.<br/>
Od djeteta, što zovu gerila,<br/>
Izrasla je partizanska sila.<br/>
Tri sestrice porodila nana,<br/>
Kompartija, majka partizana;<br/>
Tri sestrice, do tri divizije,<br/>
Teško j’ reći koja bolje bije:<br/>
Šesta lička, Sedma banijanska,<br/>
Il’ blizanka Osma kordunaška.<br/>
Kada koja kroz Kordun se kreće,<br/>
Po četama narod sipa cvijeće.<br/>

<p>(Odlomak iz pjesme Dragića Magdića, objavljeno u Narodne pjesme Korduna, sakupio i uredio Stanko Opačić-Ćanica, Zagreb: Prosvjeta, 1971, str. 231.)</p>

