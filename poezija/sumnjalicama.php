<h1>Desanka Maksimović - Sumnjalicama</h1>

Vi što mrzite i sumnjate<br/>
u činjenice, u snove, u reči,<br/>
što ne verujete ni svome sinu,<br/>
ni svojoj kćeri,<br/>
što ne vidite domovinu<br/>
kako raste i kako se leči,<br/>
što se smejete tuđoj veri;<br/>
svi vi što mrzite i što sumnjate,<br/>
što proričete propast skoru,<br/>
pustolovinom zovete svetla pregnuća<br/>
i mrakom zoru,<br/>
što ne poštujete ni delo čiji<br/>
temelj krv je zalila vruća;<br/>
svi vi što tvrdite da lokomotive<br/>
nikad neće proći omladinskom prugom,<br/>
da će se srušiti most iza mosta,<br/>
jer nije zidan rukom stručnom,<br/>
što govorite o svemu s lažnom tugom<br/>
i pakošću žučnom,<br/>
pođite ipak na tu prugu<br/>
da neviđene vidite slike!<br/>
Brže nego do Amerike,<br/>
nego do tuđinskih hladnih mora,<br/>
tamo ćete stići, –<br/>
samo jedra treba na srcu dići,<br/>
i voleti miris domovinskih zora.<br/>
Videćete je kraj sebe živu<br/>
i potok čelični zahuktali,<br/>
i cvećem iskićenu lokomotivu,<br/>
putnike iz Šamca i Sarajeva, –<br/>
i kao svici varnica pleva<br/>
svaki čas se gasi i pali.<br/>
<br/>
Okušajte joj rođenom rukom čvrstinu<br/>
i svojim okom zgledajte lepotu,<br/>
a tada i bez vaše volje<br/>
lepota njena će stati da vas dira,<br/>
i pojmićete kako ljubav bolje<br/>
nego cement ikoji cementira.<br/>
<br/>
Takva će vam radost tad biti dana<br/>
da radujete se uza svog sina,<br/>
da shvatite kako ima građevina<br/>
lepših nego sve što zidaju stranci,<br/>
srca u temelj uzidana<br/>
da čvršće stoje nego kamen-stanci.<br/>
Ali s mržnjom i sumnjom na toj stazi<br/>
ne stojte dok vam je glava živa,<br/>
ne stojte kad čujete točkove brze,<br/>
jer strašnije nego svi vozovi strani<br/>
ta cvećem okićena lokomotiva<br/>
s pesmom na usnama može da zgazi<br/>
sve koji sumnjaju i koji mrze.<br/>
<br/>
I svaki se seća, i svaki se seća:<br/>
bila je kao cvetna grana mlada<br/>
i kao metak odlučna i brza,<br/>
i budila se hukom vodopada,<br/>
a sada spava<br/>
u senci bukava.<br/>
<br/>
(1948)<br/>

