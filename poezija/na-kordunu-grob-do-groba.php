<br/>
Grobak se je otvorio,<br/>
sin je majci govorio:<br/>
“Ne plač’ mila majko moja,<br/>
teška mi je suza tvoja.<br/>
Teža mi je suza tvoja,<br/>
nego crna zemlja moja.<br/>
