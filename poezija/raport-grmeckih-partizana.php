<h1>Branko Ćopić - Raport grmečkih partizana</h1>

Drugarice i drugari,<br/>
poznanici naši stari,<br/>
branioci zemlje ove,<br/>
evo Vama pjesme nove.<br/>
<br/>
Novih stvari dosta ima,<br/>
i na fronti i selima,<br/>
vrijedna raja ta seljačka,<br/>
samo nešto mnogo pljačka.<br/>
<br/>
Avioni došli štabu<br/>
bombarduju našu ćabu,<br/>
pobacaše silne kante<br/>
rašćeraše zabušante.<br/>
<br/>
Komesar je prvi strugno<br/>
za njim štabski stražar šmugno,<br/>
pisar Drago junak bio<br/>
pod krevet se on sakrio.<br/>
<br/>
Dok kod štaba bombe biju<br/>
na frontu se borci smiju,<br/>
nek vide iz pozadine<br/>
da sad nema hladovine.<br/>
<br/>
Prema Sani oganj gori<br/>
konjička se četa bori,<br/>
junaci su, nema šale,<br/>
samo kuća da ne pale.<br/>
<br/>
Neki voli i u pljačku<br/>
al nek skine našu značku,<br/>
pljačka ti je baš sramota,<br/>
ustaška je to rabota.<br/>
<br/>
Na Kljevcima rusvaj čine<br/>
partizanske desetine,<br/>
svud ustaše bježe mlade<br/>
Dragijne se boje brade.<br/>
<br/>
U Rujiškoj dobro stisli<br/>
jer Ličina gadno čisti,<br/>
odvali ti kao s kruške<br/>
ne treba ti gore puške.<br/>
<br/>
I opština jedna ima<br/>
u njoj sedi čiča Sima.<br/>
Šteta što je stari čika<br/>
ima ženskih vijećnika.<br/>
<br/>
Pred Gudavcem, za nesreću,<br/>
Talijani mnogo šeću,<br/>
al im smeta' jedna brana<br/>
jer tu ima Risovčana.<br/>
<br/>
Fašisti iz Krupe vire<br/>
na kuruze i krumpire,<br/>
slatka im je muka naša<br/>
al se boje roguljaša.<br/>
<br/>
U varoši gladni sjede<br/>
dok se kod nas dobro jede,<br/>
ni drveta kod njih nije<br/>
dok nas, braćo, Grmeč grije.<br/>
<br/>
S Pavelićem gadno stalo<br/>
sve mu jadno i propalo,<br/>
nema više vjere u se<br/>
krepalo mu bojno kljuse.<br/>
<br/>
Talijanu druge nema<br/>
na Rusiju vojsku sprema,<br/>
idu tamo silna braća<br/>
al se malo koji vraća.<br/>
<br/>
Ispred Moskve gadno zvoni<br/>
ginu tamo milioni,<br/>
krvava je stopa svaka<br/>
fašizmu se kopa raka.<br/>
<br/>
Oj, beskrajne ruske ravni,<br/>
Oj, narode, radni, slavni<br/>
Smrt fašizmu, dolje tama<br/>
napred, braćo, svi smo s vama.<br/>
<br/>
(1941)<br/>

