Naš dom gori, naš kmet beži,<br/>
v stiski smo se našli vsi!<br/>
Kar koli kdo: meščan rudar —<br/>
rojak je naš, naš brat vsekdar.<br/>
<br/>
Kar je bilo, več ne velja,<br/>
zatrli duh smo suženjstva.<br/>
Vstal je nov, svoboden rod,<br/>
nič več tlačan, sam svoj gospod.<br/>
<br/>
Od Mure tam do soških Brd<br/>
prinašamo izdajstvu smrt.<br/>
S krepko pestjo odrešimo<br/>
zalege tujske vso zemljo.<br/>
S svojo krvjo ustvarjamo<br/>
Zedinjeno Slovenijo.<br/>
<br/>
Ne štejte nas, ni važno to,<br/>
saj vsak od nas velja za sto.<br/>
Za našo stvar, veliko stvar,<br/>
edini smo kot še nikdar.<br/>
<br/>
Od Pirenej, kjer Bask živi,<br/>
do tja, kjer Leningrad stoji,<br/>
ves svet je naš, vsi eno smo,<br/>
vsi, ki svobodo ljubimo.<br/>
<br/>
Za novo pravdo borci smo,<br/>
za sveto misel bratovsko.<br/>
Življenje zanjo tvegamo,<br/>
zvestobo ji prisegamo.<br/>

