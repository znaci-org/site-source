Ispod naše gore Velebita.<br>
<br>
I pod jelom ranjen junak leži,<br>
Drugarice hajd mi rane veži.<br>
<br>
Ja mu rane vezujem u ruže,<br>
I pitam ga kako ti je druže.<br>
<br>
Teške su mi oko srca rane,<br>
