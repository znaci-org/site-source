na kojima su kucale satima,<br/>
i polaze;<br/>
mladići ih čekaju pred vratima,<br/>
i o budućnosti bezbrižno snove snuju,<br/>
jer misle da će se skoro uzeti.<br/>
<br/>
Samo kupljeni kovači kuju, kuju, kuju<br/>
lance... lance...<br/>
za nas, za žive brojeve<br/>
od jednog do dvesta,<br/>
koji moramo sutra nestati,<br/>
da drugima pravimo mesta...<br/>
