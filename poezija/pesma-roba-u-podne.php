Peva u podne tamni rob<br/>
sa tihom setom:<br/>
<br/>
- Sve me svakog dana daska više seče,<br/>
i sve mi je manje odmora,<br/>
i sve mi svakog dana kosti strašnije zveče;<br/>
vidim da ću sa ovom čežnjom za svetom,<br/>
i za zrakom, i za majkom,<br/>
pasti u Crno, kad dođe smena.<br/>
<br/>
Sada je podne. U toplom zraku lete<br/>
velike žene i male midinete<br/>
na ručak; i smeju se potom,<br/>
i žive čudnim, slobodnim životom,<br/>
a ja sam bled, kukavan rob.<br/>
<br/>
Sada se svuda gase kandila,<br/>
i zvone poslednji amini...<br/>
