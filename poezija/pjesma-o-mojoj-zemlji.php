u kojima me majka zibala,<br/>
svjetlost tvoju — ovo proljeće što je moralo doći,<br/>
proljeće tvoje i naše s traktorom preko njiva.<br/>
Lijepa si, zemljo moja, kao sloboda, kao oči<br/>
<br/>
tvojih junaka što i preko smrti gledaju.<br/>
Koliko mrtvih pod tvojim rosnim travama.<br/>
I kad ih vjetar češlja — oni pjevaju,<br/>
i u grlu tvojih rijeka — oni pjevaju,<br/>
i u pjesmi tvojoj — oni pjevaju,<br/>
