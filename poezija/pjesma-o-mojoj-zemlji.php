brane tvoju istinu.<br/>
I kad rudari u utrobu zemlje silaze —<br/>
brane tvoju istinu.<br/>
I kad strojevi štampaju »Manifest« i »Oktobarsku« —<br/>
brane tvoju istinu.<br/>
I kad te gledam buduću —<br/>
ja pjevam tvoju istinu.<br/>
Pjevam tebe, ali još više snagu koja te rodila.<br/>
