<h1>Jure Kaštelan - Pjesma o mojoj zemlji</h1>

1<br/>
<br/>
Lijepa je ova zemlja. Meni najdraža. Nigdje toliko algi<br/>
kao u uvali moga djetinjstva. Livade i konji nesedlani.<br/>
Koliko rijeka i slapova. Dođi, prijatelju.<br/>
Mi smo na Balkanu. U Jugoslaviji.<br/>
<br/>
Podijelit ćemo hljeb i radost i tugu ako si tužan.<br/>
Mi znamo kako rana peče i kako boli nož.<br/>
Ima i jablanova do neba i sunca za sve slikare i kupače<br/>
i bistrih voda brzica. Vidjet ćeš sam.<br/>
<br/>
Vidjet ćeš zemlju svu na dlanu. Takvu kakova jeste.<br/>
I zgarišta gdje su bila. Sve ćemo ti reći.<br/>
Tamo kod novogradnje: još juče vješala,<br/>
gdje smo sa Titom ustali prkoseći.<br/>
<br/>
Znaš gdje je Balkan, Jugoslavija — na Balkanu.<br/>
Zemlja socijalizma. Možeš i vodom i zrakom. Dođi i vidi.<br/>
Istina je naša krvlju zapisana.<br/>
Gledaj i mjeri kuda ti se svidi.<br/>
<br/>
Lijepa je ova zemlja. Meni najdraža. U njoj smo,<br/>
daleki druže, i za te umirali.<br/>
Lijepa je ova zemlja. Socijalizam na Balkanu.<br/>
U njoj smo krv i snove i za te, druže, dali.<br/>
