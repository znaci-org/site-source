na brdu rascvjeta granata aršlama.<br/>
<br/>
I brigada leti u bitku. U novu. Još bješnju.<br/>
I već smo na koti. I barjak crveni pred svima.<br/>
A proljeće na drugoj pobija košulju-trešnju.<br/>
I trešnju, sprijeda, topovski magnezijum snima.<br/>
<br/>
I gdje je umor? I glad? Ni umora, ni brda, ni gladi!<br/>
U nama naviru izvori. Uvis nas začikuju čuke.<br/>
A proljeće juri i juri — ostavi košulju, drugu gradi,<br/>
il' put kazuje, il' grabi muštuluke.<br/>
<br/>
O, nikad, nikad ko danas — proljeće četrdeset treće —<br/>
ja i zemlja ova nismo bili tako jedno, tako isto biće;<br/>
nikad proljeće njeno nije bilo toliko i moje proljeće,<br/>
i klice zajedničke, i patnje, i koža, i mišićje.<br/>
<br/>
I čuj: svaki sekund me boli, svaki cvijetak, svaki zarud,<br/>
što s nama nijesu — da skupa stignemo svakoj trešnji,<br/>
da koracima brojimo otete topovske cijevi po Ivsaru,<br/>
