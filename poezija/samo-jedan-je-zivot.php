<h1>Radonja Vešović - Samo jedan je život</h1>

Samo jedan je život,<br/>
i ja neću koračati po njemu —<br/>
plašljivo, kao po žici,<br/>
raskriliću ruke i misli široko, široko,<br/>
koliko su prostrani vidici.<br/>
<br/>
I proći ću kroz drvorede dana,<br/>
sigurnim korakom, u bojnome redu, ——<br/>
ozaren, što sva kugla gori pobunama:<br/>
raspjevan, od vjere u pobjedu.<br/>
<br/>
I dani htjeo bih da budu — velike ore;<br/>
duša melemne nježnosti, ko saće puna da bude;<br/>
osamljene, tužne, da vodi na razgovore;<br/>
klonule u borbi da pridigne ljude.<br/>
<br/>
I da u svaki dan utkam po dio sebe:<br/>
srdačno da ga ispratim u radu višesatnom.<br/>
da ne kopa po duši misao zaostala, što zebe<br/>
dugom prema danu — prošlom, nepovratnom.<br/>
<br/>
Samo jedan je život. Zato — izgarati što jače!<br/>
Da ne zažalim na kraju puta za njime:<br/>
„Ej, kad bih se snova rodio, počeo bih drugače“<br/>
a grob leži preda mnom pun leda i praznine<br/>
<br/>
I ako je određeno na kraju,<br/>
čovjeka jedan dio zemlja da ima,<br/>
ja bih najbolji dio mene, da ostane —<br/>
ugrađen, zavjetovan ljudima.<br/>
<br/>
Samo jedan je život,<br/>
a za snove srce više života hoće;<br/>
i noću i danju nemiran sam zbog toga,<br/>
pa bih da zbijem dane,<br/>
da pucaju od punoće,<br/>
jer godine ne čekaju nikoga.<br/>
<br/>
Samo jedan je život,<br/>
i ne treba puziti po njemu<br/>
plašljivo, kao po žici;<br/>
treba smjelosti<br/>
mislima i snazi dati onoliko —<br/>
koliko su široki vidici.<br/>
