<h1>Josip Cazi - Na klancu</h1>

Tamo kod Trnavca,<br/>
gdje se nad Klancem stijene dižu,<br/>
gole litice i oštre vrleti,<br/>
tamo gdje se uz cestu<br/>
popaljene kuće ko grobovi nižu,<br/>
brijegovi sniježni, nikad neoteti –<br/>
– čuj kako vihor u osvetne dane,<br/>
nemilo goni smrznute oblake<br/>
i bije ledom ličke partizane.<br/>
<br/>
Domovino majko, sinovi Ti slavni,<br/>
ko džinovi se penju uz krševe gole<br/>
i brane životom jer Te žarko vole,<br/>
Tvoje vite gore i pitome ravni.<br/>
<br/>
Selo Klanac gori, tuđin ga zapali<br/>
stoka bolno muče, djeca suze liju,<br/>
fašisti im majke u kola uprežu,<br/>
nemoćne starce lancima vežu,<br/>
očeve, braću kundacima biju –<br/>
plijen za sobom vuku i krv liju.<br/>
<br/>
Oj, koliko bola i krvavih priča piše se u Lici:<br/>
Partizani gdje ste, gdje ste osvetnici?<br/>
<br/>
Tamo kod Šijanovog klanca, gdje bjelasaju sniježni vrhovi,<br/>
po ledu i kršu gdje se rađaju orlovi,<br/>
tamo je četa Orešković Marka, junaka ratnika smjela,<br/>
niz klance<br/>
i gudure,<br/>
crne košulje rušila<br/>
sa hiljadu strijela.<br/>
<br/>
Šijanov klanac u Lici, 5. 9. 1942. g.<br/>
