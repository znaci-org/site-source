<h1>Desanka Maksimović - Otadžbino, tu sam</h1>

<i>
  Posvećeno Dušici Stefanović<br />
  mladoj naučnoj radnici, streljanoj pod<br />
  nemačkom okupacijom u oktobru 1941. godine<br />
</i>
<br />
Ćutite trenutak, grmuše i laste,<br />
čija srca prva jesen tugom puni,<br />
neka tišina duboka naraste<br />
tu gde prostrane otvoriše jame<br />
nemci, najamnici njini i špijuni.<br />
<br />
Utišaj se, vetre, dva jutarnja trena,<br />
vetre oktobarski u granama zova,<br />
u krčevini brestova i klena,<br />
u ovaj čas je smrtno pogođena<br />
ona na strelištu s dvadeset drugova.<br />
<br />
Pala je prve jeseni ustanka,<br />
kada je jasno oktobarsko zlato<br />
poprskalo goru,<br />
kad se uveliko brala loza ranka,<br />
i žene s decom spremale da oru.<br />
<br />
Ostavi igru, mali čobanine,<br />
zastani ovde dva kratka trenutka,<br />
posmatraj kako žuti prvo granje, –<br />
ovakvo jesenje bilo je svitanje<br />
kad ona, čobane, zanavek zaćuta.<br />
<br />
I vi, ljudi, čiji domovi se dime<br />
pokraj jajinačkog krvavog stratišta<br />
koji ste čuli poslednje reči njine,<br />
ćutite, ćutite trenutak u ime<br />
cele domovine.<br />
<br />
Možda ćete začuti pesmu, reči:<br />
brda u jesen ponavljaju jeku,<br />
čuje se davni mitraljeski pucanj<br />
i glas jedan mladi u tome odjeku:<br />
„Otadžbino, tu sam!“<br />
<br />
....................................<br />
<br />
Pred univerzu konjica dotopta,<br />
zveknu kao gvozdena lesa,<br />
i u sjaj prozora odakle studenti<br />
nasilju prete<br />
za opomenu pucanj skresa,<br />
diže se dima bela lopta.<br />
<br />
A mladost prkosi, peva, lupa,<br />
kao da skida sebi s vrata<br />
vremena loša.<br />
oj, što su jake ruke studenata!<br />
oj, što su gromka grla u brucoša!<br />
<br />
Na jednom prozoru po stotinu glava,<br />
po sto smelosti u prkosu.<br />
I pucanj u dimnjake opet osu,<br />
i buka slična nepogodi<br />
opet se diže<br />
kao da u srce najdražeg druga<br />
metak zgodi.<br />
<br />
Na prozoru prirodnjačkog seminara<br />
omalena devojka crnpurasta<br />
niče tada.<br />
podiže pramen što na čelo pada<br />
