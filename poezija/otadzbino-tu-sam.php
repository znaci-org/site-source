<br />
Zveket oružja zagluši ljudske reči,<br />
izmešaše se s rukama noževi goli,<br />
nasta jauk, trka.<br />
Zid oružan, suri, devojku opkoli,<br />
vidi joj se samo zamršena kosa,<br />
ko žbun gusta, mrka.<br />
<br />
Tamnička česma učmalo kaplje,<br />
