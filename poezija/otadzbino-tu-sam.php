u ime slobode, mržnje protiv sila,<br />
u susret ko zna kojoj opasnosti.<br />
<br />
S policijom je sad oči u oči.<br />
<br />
„Kakvu to slobodu ona još zahteva!<br />
Veži jezik curi!“<br />
