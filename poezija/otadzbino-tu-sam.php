<br />
Da je samo otadžbine što bi znala<br />
od svakog čoveka da stvori druga,<br />
u kojoj srce okrilati;<br />
otadžbine koja ne bi mogla<br />
s metkom i psovkom<br />
pred školu stati.<br />
<br />
Da je samo slobode one lepe<br />
