<br />
Neće studente goniti žbiri,<br />
neće ih satirati tamnice, tuče;<br />
zemlja će ići na prstima<br />
dok oni uče,<br />
da ih ne omete, ne uznemiri.<br />
<br />
Tiho će po moru gaziti lađe,<br />
razboji će lupati tiše,<br />
da naučnik mogne misao pravu,<br />
željenu da nađe,<br />
da mogne u miru da je zapiše.<br />
<br />
Kad otadžbina njena jednom bude<br />
zemlja o kakvoj sada sanja,<br />
sve će straže,<br />
sve vojske od noći do razdanja<br />
bdeti nad onima koji do istine<br />
puteve traže.<br />
<br />
Gle, neki stvorak sićušni, živi,<br />
na dlan joj pade,<br />
ogrtač tanki srebrnosivi,<br />
tu raširi<br />
i lepršati njime stade.<br />
<br />
Prekide joj te tople misli,<br />
na tamnicu opet opomenu;<br />
spazi osam malih, modrih kvadrata,<br />
zidove što se oko nje stisli,<br />
ču hod stražarev iza vrata.<br />
