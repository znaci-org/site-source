kao juče,<br />
nju strašne, bunovne slike muče:<br />
policija naoružana<br />
juri na školu s hiljadu ruku.<br />
<br />
U knjige zabada bajonete,<br />
epruvetice lomi lake,<br />
na pastrvice juriša s kamom,<br />
diže kundake<br />
na zverke i ptice punjene slamom.<br />
<br />
Razasuli se milioni<br />
slova sličnih vojskama mravi,<br />
ispred surovih gonilaca<br />
u pukotine beže poda;<br />
otržu se leptiri sa čioda<br />
i drhte u stravi.<br />
<br />
Kad se jednom ostvare njeni snovi,<br />
misli malena studentkinja,<br />
