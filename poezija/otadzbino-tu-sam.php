ukine smesta, iz zamaha.<br />
<br />
„Slobode!“ ona gotovo zavapi,<br />
svetla, krilata od poleta.<br />
I svak oseti<br />
u tom času:<br />
za sve što kaže ona će mreti,<br />
dati se do poslednje kapi.<br />
kako je drugovi pogledaše toplo!<br />
kako je psovka vojnička zasu!<br />
<br />
A tamo opet zveket i fijuci,<br />
topot kopita,<br />
ptičja piska;<br />
policija drži puške u ruci,<br />
građanin uplašen hita,<br />
uz plot se tiska.<br />
<br />
Padaju knjige na glavu konjanika,<br />
