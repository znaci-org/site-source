<h1>Vladimir Nazor - Osamljeni dub</h1>

Sami smo ... Neka! Sami uv’jek bjesmo<br/>
Ko dub što stoji na proplanku svom;<br/>
I rasli smo, i listali; uzalud<br/>
Dugo nas bio vjetar, grad i grom.<br/>
<br/>
Svoje nas zemlje hranio je kremen,<br/>
Od svoga soka gradili smo srč,<br/>
I nikad nas ukočila nije<br/>
Mrzlina straha il’ zdvajanja grč.<br/>
<br/>
I hrast naš ima svoje grane i lišće;<br/>
U njemu zuji naših pčela let,<br/>
A voda, koja izvire pod njime,<br/>
Ne stoji, teče u taj b’jeli sv’jet.<br/>
<br/>
Teče daleko, u dole, u luge;<br/>
Naći će kor’jen koji žedni, vlat<br/>
Što sahne; opet iz iste čaše<br/>
Da iz nje pije otuđeni brat.<br/>
<br/>
Sami smo, ali za široko srce<br/>
Samoće nema. Što već u njem bje,<br/>
Ostaje: ono nalik je na gn’jezdo<br/>
Što čuva što se u njem rodi pre;<br/>
<br/>
Čuva i čeka da se laste vrate<br/>
Uz pjesmu novu, u nov Đurđevdan.<br/>
— Dub stoji sam na proplanku i znade<br/>
Proći će sve — pa i taj ružni san.<br/>
