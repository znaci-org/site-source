<h1>Antun Barac - Časovi klonuća</h1>

Onemogle ruke. Noga jedva gazi.<br/>
Zaklopljene vjeđe – da iščezne zbilja.<br/>
Srušene su ceste, pali putokazi.<br/>
Život nema pravca, ni smisla, ni cilja.<br/>
<br/>
Stare laži novim zovu istinama,<br/>
Plaćeno izdajstvo drže rodoljubljem,<br/>
Žuđenom slobodom svijet u tamnicama,<br/>
A put čovječanstva u ropstvu sve dubljem.<br/>
<br/>
Kako boli pogled djeteta na cesti<br/>
- Ostavljena, gladna, zaplakana lica –<br/>
I ne moći ništa – tek stiskati pesti<br/>
Pokraj zločinstava podlih ubojica!<br/>
<br/>
Gađenje i prkos, stid i očajanje<br/>
Vitlaju se dušom kao vihor ludi:<br/>
Ne videti više krađu, blud i klanje,<br/>
Pobjeći od sebe, od kuća, od ljudi!<br/>
<br/>
Urliknuti ko zvijer, prostrijeljena metkom,<br/>
glasom, koji ječi preko gora, stijenja,<br/>
Da odjekne u njem – uz bol nad svršetkom -<br/>
Sva naša sramota i sav jad življenja!<br/>
<br/>
(u ustaškom logoru 1942.)<br/>
