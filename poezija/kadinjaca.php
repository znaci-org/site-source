<h1>Slavko Vukosavljević - Kadinjača</h1>

<p>(odlomak)</p>

A na Kadinjači breze su vitke<br/>
k’o borci ranjene legle uz breg.<br/>
Tišina, tišina, zatišje bitke,<br/>
na suve usne kaplje sneg.<br/>
…<br/>
<br/>
Komandantovu pogledaj ruku –<br/>
pod njom bataljon i ceo breg;<br/>
sve bliže, sve jače topovi tuku<br/>
i tiho, tiho pada sneg.<br/>
<br/>
– Prva četa, tamo kod breze!<br/>
Druga levo, na prevoj, niže.<br/>
Prateći vod – šumi polako.<br/>
Pozadi desno mitraljeze.<br/>
Dobro je, drugovi. Tako, tako..<br/>
<br/>
On je polako spustio ruku.<br/>
Namah je zamuk’o breg,<br/>
samo što napred topovi tuku<br/>
i tiho, tiho pada sneg.<br/>
<br/>
I napred upro bataljon oči<br/>
– tako se gleda s’ burom sve jačom –<br/>
k’o da će sada ćutke da skoči<br/>
zajedno s’ brdom, s’ Kadinjačom.<br/>
<br/>
A tamo dole, još daleko,<br/>
crna se zmija okukom povi<br/>
i s’ čudnim mirom reče neko:<br/>
– Pazi, tenkovi!<br/>
<br/>
I k’o da tiho niz polja bela<br/>
uz odjek topova što nebo riju,<br/>
topao kao krv iz tela<br/>
poteče šapat u Srbiju:<br/>
<br/>
– Evo, ja ovde, drug u blizini<br/>
i ovo brdo i tišina –<br/>
da li je tako, il’ mi se čini –<br/>
sve je to sada Domovina!<br/>
<br/>
Znam da čete Sandžaku žure<br/>
a nama ko zna šta se sprema;<br/>
evo Nemaca, tenkovi jure,<br/>
a bombi nema!<br/>
<br/>
I nije čudo, nije čudo –<br/>
običan taster za to je malo –<br/>
srce dečaka uvek budno<br/>
Srbiji što je otkucalo:<br/>
<br/>
– O, čujte čete u daljini,<br/>
budući ljudi, zemljo nova,<br/>
da li je tako il’ mi se čini –<br/>
svi ste kraj našeg rova!<br/>
<br/>
Kažite – tu smo!<br/>
Kažite – jesmo!<br/>
Kažite budući,<br/>
Kažite živi!<br/>
Dižite pesti, poleti pesmo,<br/>
pođite, sunca, na oblak sivi!<br/>
Vadite, ljudi, srca iz grudi!<br/>
O, kaži i ti, bar šapni, mama:<br/>
– Svi smo sa vama!<br/>
<br/>
Al’ stroj se ceo diže i krenu;<br/>
pognu se, jurnu i dečak bled<br/>
jer komesarov krik ga prenu:<br/>
– Napred, napred!<br/>
<br/>
I sve najednom promeni lik,<br/>
poče da tamni, da čili dan<br/>
i sve se stopi u zov, u krik,<br/>
prigušen, dalek, k’o tuđ a znan:<br/>
<br/>
– Za nove dane – da svima svane!<br/>
Da ove staze pošteni gaze!<br/>
Da više nema groba do groba<br/>
ni gladi pokraj kućnoga plota,<br/>
da nema tame, da nema roba,<br/>
da bude toplo od života!<br/>
U cevi ljubav,<br/>
mržnju i jed –<br/>
napred,<br/>
napred!<br/>
…<br/>
<br/>
Rođena zemljo, jesli li znala<br/>
– tu je pogin’o bataljon ceo;<br/>
crvena krv je procvetala<br/>
kroz snežni pokrov, hladan i beo.<br/>
Noću je i to zavej’o vetar.<br/>
Ipak, na jugu vojska korača:<br/>
Pao je četrnaesti kilometar,<br/>
al’ nikad neće – Kadinjača!<br/>


