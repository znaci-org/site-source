<p>
  Pišući pjesmu <i>Moj grob</i>, Goran Kovačić je možda u stihovima htio iznijeti samo djetinjsku privrženost prema svojim planinam a Gorskoga kotara — i od­bojnost prema lažima društva, koje se nad grobo­vima voli licemjerno raznježiti. Tada vjerojatno nije ni pomislio, da će mu grob doista biti u planinama — ali ne u planinama njegova zavičaja, već negdje u Bosni. Izričući želju, da mu k posljednjem počivali­štu dolaze samo dobri prijatelji, a da ostali ljudi uopće ne saznadu, gdje se ono nalazi, nije očito ni slutio, da će mu se nabačena želja ispuniti čak i više od potpunosti. Pišući pak o tragici intelektualca svo­ga pokoljenja, nije imao ni nakraj pameti, da će upravo on, svojim životom i smrću, biti jedno od najjasnijih njezinih utjelovljenja."
</p>

