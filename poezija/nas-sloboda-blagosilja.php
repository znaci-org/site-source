<h1>Desanka Maksimović - Nas sloboda blagosilja</h1>

Na trideset streljanih partizana<br/>
i deset nestalih bez traga,<br/>
već deset puta po trideset,<br/>
a to posle množi sa tisuću,<br/>
pa ni tu sva patnja nije zbrana –<br/>
nema mere za našu krv vruću.<br/>
<br/>
Ne selo, ni dva ni dvadeset,<br/>
već stotine sela u plamenu;<br/>
goreo je krov, i vrt, i kut gde se žanje,<br/>
i kamen na kamenu.<br/>
Ne stotine, hiljade ognjišta<br/>
kod nas je manje.<br/>
<br/>
Ne jedan grob i jedna jama<br/>
u kojoj su desetine ležale skupa,<br/>
ne jedne oči uplakane;<br/>
već cele pregršti ljudskog plača;<br/>
svaki dom treći, na njivi slama,<br/>
bili su strelište i lomača.<br/>
<br/>
Ne jedan i dva u vodu bačena;<br/>
plovili su mrtvi poput splava,<br/>
vodu svakog trećeg mutili vira.<br/>
Cela zemlja je bila tlačena;<br/>
kroz zemlju našu dušman je išao<br/>
lica crvenog kao u vampira.<br/>
<br/>
Ali to strancu mi ne kazujemo.<br/>
Udubljeni u sebe radosno slušamo<br/>
kako nam istočena snaga nabujava,<br/>
buja nam srce, i zemlja, i voda,<br/>
stabljika bilja:<br/>
krvlju naroda kupljena sloboda<br/>
nas blagosilja.<br/>
<br/>
Ne jedan i dvojica i trojica,<br/>
već po deset nas ima u jednome,<br/>
i u svakog su ruke, desetore,<br/>
i po tri srca kao usred bitke;<br/>
zatrte njive mi smo posejali,<br/>
pošumili opet iskrčene gore,<br/>
od bara vode načinili pitke.<br/>
<br/>
Svakim danom raste snaga ova.<br/>
Ne selo, ni dva ni dvadeset,<br/>
hiljade mi ćemo podići sela,<br/>
hiljade brodova iznad voda;<br/>
ne jedan i dva, hiljade mostova<br/>
preko klanaca i neprohoda –<br/>
kao da još žive pali junaci<br/>
i po deset ih uz svakog od nas hoda.<br/>

