<h1>Dušan Kostić - Zvijezde i čovjek u samici</h1>

<i>Španiji 1936</i><br/>
<br/>
Noćas<br/>
u krošnjama jablana možda<br/>
zvijezde gore.<br/>
Ja ne znam — al’ osjećam da oganj<br/>
njihov na kuće pada, na srca, ma ruke,<br/>
na oči koje zbore.<br/>
<br/>
Pred mojom rešetkom koraci<br/>
i blijesak noža.<br/>
<br/>
Ja zvijezde aprilske ne vidim,<br/>
al’ osjećam da se s njih stače<br/>
otsjaj daleke krvi.<br/>
Daleko<br/>
kraj mora —<br/>
i noćas,<br/>
i juče i sjutra<br/>
ljudima sa suncem u grudima,<br/>
ljudi sa buntom u očima,<br/>
kao gora<br/>
padaju,<br/>
padaju,<br/>
padaju.<br/>
<br/>
A ja sam mlad, a ja sam sputan,<br/>
ja sam sam.<br/>
Ti teški koraci što se lome sve istom stazom,<br/>
ti mokri zeleni zidovi,<br/>
u kasne ure<br/>
prituljeni mukli ljudski krikovi;<br/>
— je li to moja mladost,<br/>
moji purpurni snovi?<br/>
<br/>
Noćas se drugovi bore, a ja sam daleko<br/>
(iz ruku im pada sjeme za suncem natopljen dan),<br/>
noćas zvijezde gore<br/>
krvlju —<br/>
ljudskom krvlju! —<br/>
a ja ih ne vidim, a ja sam zakopan.<br/>
O proklete ruke što me k’o mora stežu,<br/>
proklete krvave ruke što me za rešetke vežu!<br/>
Ja sam sam,<br/>
sputan,<br/>
nemoćan.<br/>
Zatvaram oči i u noć mi se ovu čini:<br/>
eto me, drugovi, eto,<br/>
pusta je moja ćelija daleki brod na pučini!<br/>
