(<i>Petru Komneniću</i>)

<p>I</p>

<p>Sloboda ždere meso i krv guta,<br>
Sloboda noge krši, lomi ruke.<br>
Darovi njeni groblja su i muke.<br>
Sloboda je strašna, hladna i kruta.</p>

<p>Njena su vojska kosturi, mrtvaci;<br>
Njena su vrata grozne smrti ralje,<br>
A poderane njezine su halje,<br>
I crni od krvi i dima barjaci.</p>

<p>Sloboda riče ko bombe, granate,<br>
Ko tanad pišti, i strašno se ceri<br>
Ko nebo, kojim kruže bombarderi.<br>
Oko nje crni gavrani se jate.</p>

<p>Sloboda nema krila, ima štake,<br>
I oka nema, ćorava i slijepa.<br>
Ko sablast ide. Ona nije lijepa:<br>
Ona je strašna ustala iz rake.</p>

<p>Ljuljačke njene vješala su. Tane<br>
Poljubac njezin u krvave rane.<br>
Al groblja su joj brda i doline,<br>
Vrh kojih sunce nestaje i sine.</p>

<p>II</p>

<p>Slobodo, sada Klancem Smrti gaziš,<br>
A kljuse ti je ranjavo, i pada.<br>
Koščatu ruku daj nam, pa ćeš tada<br>
Od naše krvi da se preobraziš.</p>

<p>Ti nisi žena, snaga ti je muška,<br>
Ne pjevaš pjesme, al i ne naričeš:<br>
Ti grmiš, vrištiš, proklinješ i ričeš,<br>
Ko naša bomba, mitraljez i puška.</p>

<p>Puži, Slobodo, ne treba da letiš,<br>
Nek tvoje kosti pokrivaju krpe,<br>
Al jednom iznad naše mrtve hrpe<br>
Životom ćeš se Smrti da osvetiš.</p>
