<h1>Nas dva brata, oba ratujemo</h1>

<p>(<i>narodna pjesma</i>)</p>

<pre>
Herojska se borba razigrala,
Kozara se slavom ovenčala.

Nas dva brata oba ratujemo,
Ne plač’, majko, ako poginemo.

Mi smo braća ispod Kozarice,
Đe ne rađa majka izdajice.
</pre>

<p>(Savo Orović, ratni dnevnik 1941-1945 (str. 284), Beograd 1972.)</p>

<h2>II verzija</h2>

Nas dva brata oba ratujemo,<br />
Ne plač majko ako poginemo.<br />
<br />
Ne plač majko kad u borbu pođem<br />
Već ti plači ako ti ne dođem.<br />
<br />
Mi smo braća ispod Kozarice,<br />
Gdje ne rađa majka izdajice.<br />
<br />
Grmeč goro, gusta jelovita,<br />
Kraj Drvara stojiš ponosita.<br />
<br />
Tvoje jele šire svoje grane,<br />
I čuvaju mlade partizane!<br />

<h2>III verzija</h2>

Nas dva brata, oba ratujemo<br />
Ne žal' majko, ako poginemo.<br />
<br />
Ja sam braćo, ranjen među vama,<br />
Nemojte me ostaviti sama.<br />
<br />
Sklonite me sa te uske staze<br />
Da me švapski konji ne pregaze.<br />

<h2>IV verzija (Nas tri brata sva tri ratujemo)</h2>

Nas tri brata, sva tri ratujemo,<br />
Ne žal', majko, ako poginemo.<br />
<br />
Ne plač', majko, kad u borbu pođem,<br />
Već ti plači ako ti ne dođem.<br />
<br />
Petokraka, zašto si crvena?<br />
Zato što si u krvi stečena.<br />
<br />
Mila majko, ti ne žali sina,<br />
On je pao tukuć', dušmanina.<br />
<br />
Tukuć', zv'jeri - N'jemce i ustaše,<br />
I četnike, krvopije naše.<br />
<br />
Al' borbena naša omladina<br />
Osvetiće tvoga palog sina.<br />

<h2>V verzija</h2>

Pjevaj, majko, kad u borbu pođem,<br/>
al' ne plači ako ti ne dođem.<br/>
<br/>
Mila majko, to je tvoja dika,<br/>
što ti imaš narodnog vojnika.<br/>
<br/>
Ja se borim i drugovi moji,<br/>
da fašizam više ne postoji.<br/>
<br/>
Neću žalit' svoje mlade krvi,<br/>
za slobodu proliću je prvi.<br/>
<br/>
Moga brata ranila granata,<br/>
žao mi je što i mene nije.<br/>
<br/>
Nas dva brata oba ratujemo,<br/>
ne plač' majko ako poginemo.<br/>

<p>(Kotar Vrginmost u NO borbi, Beograd 1980.)</p>