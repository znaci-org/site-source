<p>(<i>Narodna epska pjesma sa Korduna</i>)</p>

Zagrmilo od Karlovca grada,<br />
Bježi narod, svuda strah zavlada,<br />
Tutnji zemlja na sjevernoj strani,<br />
Na Kordun se kreću Talijani.<br />
Prasak stoji, od pušaka jeka:<br />
Opet, braćo, ofenziva neka.<br />
Vatre gore, dimovi se dižu,<br />
Talijani u Tuš'lović stižu.<br />
Dve hiljade tu je sile vojne,<br />
Razvili se u redove bojne.<br />
Od Korane od Babine gore<br />
Po Tuš’lov’ću mnoge kuće gore.<br />
Tako, braćo, do Krnjaka stižu,<br />
Od dimova oblaci se dižu,<br />
Kuće gore, trešte mitraljezi,<br />
Jadni narod u planine bježi.<br />
Još komandant Talijana veli<br />
Da će Kordun uništiti cijeli.<br />
Al’ će s’ i on ljuto prevariti,<br />
Nije Srbe lako uništiti.<br />
Ovo nije prva ofenziva<br />
I bježanje dok je glava živa.<br />
Al’ što dušman više nam kidiše,<br />
Svaki dan je partizana više.<br />
Skupljaju se hrabri partizani<br />
Po prisoju, lavi odabrani.<br />
Svaki znade borba nije laka,<br />
Al’ u svakog srce od junaka.<br />
Iz busije glas ćeš čuti: „Stani!<br />
Tu partizan svoju zemlju brani!<br />
Fašistička ruljo bez pameti,<br />
Nećeš stići određenoj meti.<br />
Ako ste nam zemlju porobili,<br />
Ali narod nijeste pokorili.<br />
Ovdje ćete kosti ostaviti<br />
Prije nego narod pokoriti!"<br />
Narod bježi, sam se razgovara:<br />
„Mili bože, šta se od nas stvara<br />
Šta li od nas rade Talijani!<br />
Jesu l’ glase čuli partizani?"<br />
Stigli glasi u Petrovu goru<br />
Toga jutra baš u ranu zoru;<br />
Drug Ćanica sitne knjige piše,<br />
Kupi vojske što se može više.<br />
Dobar junak Opačiću Ćane<br />
Pisma šalje na četiri strane.<br />
Prvo javlja Robertu junaku:<br />
„Talijani već su u Krnjaku.<br />
Brže, druže, vesela ti majka,<br />
Šalji meni odred iz Šlivnjaka!“<br />
Drugu šalje po brzom kuriru<br />
Poštić Mirku, čete komandiru:<br />
„O moj Mirko, na glasu junače,<br />
Već nam Krnjak Talijani tlače.<br />
Vodi brže Perjane junake<br />
Da bijemo hulje talijanske."<br />
Malo vrijeme za dugo ne stalo,<br />
Ko god čuo, sve je pohitalo.<br />
Prije podne toga istog dana<br />
Skupiše se čete partizana.<br />
Što se naše vojske sakupilo:<br />
Sto pedeset na broju je bilo.<br />
Talijani već su Krnjak prošli,<br />
Sve paleći do Rijeke došli;<br />
Tako banda munjevito ide,<br />
Dokle stiže dimovi se vide.<br />
Al’ da vidiš naše partizane:<br />
Dočekali crne Talijane!<br />
Poče borba kod Martinovića,<br />
Od Vratnika sve do Popovića:<br />
Boj se bije, nebo se prolama,<br />
Al’ za silu snaga nam je mala.<br />
Samo ipak sporo napreduju,<br />
Partizani teško odstupaju,<br />
Partizani svoju zemlju brane,<br />
Svaka stopa košta Talijane.<br />
Kad Ćanica na bojište stiže,<br />
S konja skoči, primače se bliže.<br />
Kada vidje kakva sila kreće,<br />
Koja s’ lako ustaviti neće,<br />
Zove brzo sa desnoga krila<br />
Perić Gnjacu, čete komandira,<br />
Milutina, čete komesara,<br />
Da sa njima dogovr učini<br />
Kako li će odoljeti sili,<br />
Što se može učiniti sade:<br />
Sto pedeset na dvije hiljade.<br />
Tu ne vrijedi računica svaka,<br />
Nego samo srce u junaka.<br />
Znade Ćane svoje partizane,<br />
Sad poznati želi Talijane,<br />
Zato gleda što Talijan radi:<br />
Dovde stig’o kao na paradi.<br />
Škrinu zubim’, naređenje šalje:<br />
„Ni koraka ne damo im dalje!“<br />
Košarića i Basara Milu<br />
Na desnom je postavio krilu,<br />
A na lijevom Rakinić je sada,<br />
Tamo j’ odred iz Donjega Skrada<br />
I Krajačić Đuro iz Šlivnjaka<br />
Sa dvadeset dobrije’ junaka.<br />
Njima Ćane naređenje šalje:<br />
U Krnjaku nek tuku žandare,<br />
A niz prugu nek duboko siđu,<br />
Talijani da nas ne obiđu.<br />
Na sredini glavnome udarcu<br />
Perića je postavio Gnjacu,<br />
Ljutu guju iz Debele kose,<br />
S njim se borci u četi ponose.<br />
Sad da vidiš Opačića Ćane,<br />
Dobrovoljce traži partizane<br />
Za juriša đe najteže bilo.<br />
Junaka se dvadeset javilo.<br />
Kada tako čete rasporedi,<br />
Svoju pušku uzima po srijedi,<br />
Jurišači bombe pripremiše,<br />
Za Ćanicom na juriš krenuše.<br />
Boj započe, gora se prolama,<br />
Svud se drži naša snaga mala.<br />
Kolonelu namršti se lice,<br />
Poletješe s glave perjanice;<br />
Bersaljeri tom se čude sade,<br />
Kakva sila njima na put stade.<br />
Al’ to, braćo, dugo ne potraja,<br />
Na sredini podiže se graja,<br />
Tu da vidiš jurišače tiće:<br />
Naletiše u Martinoviće,<br />
Talijanska tu je prethodnica,<br />
S bombama ih napade Ćanica;<br />
Oficira jednoga ubiše,<br />
Talijane u bijeg natjeraše.<br />
Tu vidješe partizani, druže,<br />
Da Talijan umije da struže.<br />
Sad da vidiš, mio pobratime,<br />
Kakva čuda Partizani čine!<br />
Kad Talijan okrenu im leđa<br />
I nastade trka preko međa,<br />
Partizani svud ih potiskoše,<br />
Do Čukura stijene doćeraše.<br />
Talijani tu se ustaviše,<br />
Pod tvrdu se stijenu zakloniše<br />
I Popović selo utvrdiše,<br />
Od svuda se u zidove zbiše,<br />
Mitraljeze teške namjestiše.<br />
Tu se čudne prilike stvoriše,<br />
Partizani dalje ne mogoše<br />
Jer oružja teškog ne imaše.<br />
Talijani tu se utvrdiše<br />
I obranu dobru napraviše<br />
Jere napred više ne mogoše,<br />
A bježati natrag ne smjedoše.<br />
Samo, braćo, kod Čukura dolje<br />
Bombe grme, tu se jošte kolje.<br />
Sa svih strana stigli partizani,<br />
Al’ se dobro brane Talijani;<br />
Pod stijenom kukanje se čulo:<br />
Sto ranjenih i mrtvih je palo.<br />
Ali i nas popala je tuga,<br />
Izgubismo najboljega druga:<br />
Kraj Ćanice pade sivi tiću<br />
Hrabar junak Miloš Petroviću.<br />
Ali evo i nevolje druge:<br />
Loši glasi stigoše sa pruge.<br />
Rakinić je naredbu izdao,<br />
Sa pruge je četu povukao,<br />
Na Čukuru borbu je gledao<br />
I umjesto da je pomogao,<br />
Iz borbe je četu povukao,<br />
C’jelo lijevo krilo nam otkrio.<br />
Al’ to brzo opazio Ćane<br />
Pa napao oštro Talijane<br />
Koji no su pod Čukurom bili<br />
Pa izdaju nijesu opazili.<br />
Ta Srbin je uvijek take sreće:<br />
Nikva muka mimo nj proći neće;<br />
Uvijek rodi novih Obilića,<br />
Al’ se uvijek nađe Brankovića.<br />
Ovaj bio oficir kod Draže,<br />
Pa sa borbom našom se ne slaže.<br />
Al’ će i on brzo dolijati,<br />
Pa izdaje neće više biti.<br />
Al’ je Ćane srca junačkoga,<br />
Spasi borce od udarca toga,<br />
Talijansku silu je razbio,<br />
Svoju živu glavu izvukao.<br />
Sada braćo, da nastavim ovdje:<br />
Tada pomoć Talijan’ma dođe,<br />
S tenkovima pod Čukur stigoše,<br />
Ranjene i mrtve pokupiše.<br />
Tad nas crna noćca rastavila,<br />
Na sve strane borba je prestala.<br />
Sad da vidiš fašističkog gada<br />
Za osvetu što uradi sada:<br />
Partizan’ma ništa ne mogoše,<br />
Već seljaka triest pohvataše,<br />
Pohvataše jadne, goloruke<br />
I na teške staviše ih muke.<br />
Mučiše ih pa ih postreljaše,<br />
U novine laži napisaše<br />
Da hiljada pade partizana<br />
U junačkoj borbi Talijana.<br />
Al’ istina ovo j’, braćo, prava,<br />
Talijane uhvatila strava.<br />
Još dva ljeta vodi s’ borba ljuta,<br />
Al’ Talijan ovud ne prošeta,<br />
Ovim putem više ne krenuše<br />
Jer krvavi Čukur upamtiše.<br />

