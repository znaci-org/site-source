<h1>Branko Ćopić - Odgovor zemljaku</h1>

I drug mi priđe, odavna znan, mio<br/>
i prijekor-pelin kao kamen pao:<br/>
"Eh, ti nas, bogme, već zaboravio<br/>
i ti — iz Bosne pobjegao!"<br/>
<br/>
O, moj Šefkete, dobričino stara,<br/>
kada ti tako, šta će drugi reći?<br/>
Pa zar i tebe srce-oko vara?<br/>
I zar se može od tuge pobjeći?<br/>
<br/>
Otišao sam, već godine ima,<br/>
poslednji pogled s avionskog krila:<br/>
ostade Bosna sa očima tužnim,<br/>
a za spomen mi — ranu ostavila.<br/>
<br/>
Od tada, eto, čim dojedri veče<br/>
i za sto sjednem, misao zaluta,<br/>
kad spomen rana ko suza zapeče,<br/>
a Bosna sjedne meni preko puta.<br/>
<br/>
San odleprša, nema više mira,<br/>
i uho čuje bez trepeta, zvuka.<br/>
Bosna u pero stane da diktira,<br/>
omađijana, pokrene se ruka.<br/>
<br/>
Šapuće Bosna: putuju brigade<br/>
i u koloni smiju se dječaci,<br/>
a smrt-vučica za njima se krade<br/>
i preko noći - izrastu junaci<br/>
<br/>
I grob za grobom osipa se redom,<br/>
ide kolona, niska prorijeđena,<br/>
prati je Bosna majčinim pogledom<br/>
i kremen-vatra trepti u dnu zjena...<br/>
<br/>
Iz noći u noć tako, moj Šefkete,<br/>
ona mi dođe poput čelik-bajke,<br/>
obiđe Bosna udaljeno dijete,<br/>
s mirnom tugom zabrinute majke.<br/>
<br/>
Ti je sad gledaš novu, nasmijanu,<br/>
vrije i bruji kao jutra južna,<br/>
dok ja još uvijek nosim staru ranu<br/>
i noći brojim uz dva oka tužna.<br/>
<br/>
Zaboravio? Kad god dignem glavu,<br/>
a Ona sjedi ukraj stola moga.<br/>
Kosovka prava, krv joj na rukavu...<br/>
a ja sam imo brata rođenoga.<br/>
<br/>
Bosna i danas vodi moju ruku,<br/>
a to o bjekstvu — nemoj više reći.<br/>
I kad se ode, stare staze vuku.<br/>
A zar se može od tuge pobjeći?<br/>
