<h1>Desanka Maksimović - Bajka o ustaničkoj pušci</h1>

Cela planina stade da kuca<br/>
ustaničke puške srcem mladim,<br/>
njezinim glasom da se smeje.<br/>
Reke se stvoriše u bojne pesme,<br/>
studene špilje u tople streje,<br/>
ptice u nove ustanice,<br/>
u izvidnike pastiri redom<br/>
kod ovaca;<br/>
dubovi stari, krševi, čuke,<br/>
dobiše ruke,<br/>
i svaka kamen u ponor baca.<br/>
Planina sva se preobrazi,<br/>
oživele puške strašnim glasom,<br/>
u divljoj snazi,<br/>
jeknu od vrha do ponora.<br/>
Oslušnu neprijatelj sa užasom<br/>
tu pretnju što je reče gora.<br/>
<br/>
– Zemljo, otvori se! –<br/>
zaklikta puška.<br/>
Glas se otište niza strane,<br/>
odzvaše se puške zakopane,<br/>
prva, treća, i trideseta,<br/>
iz cele zemlje, iz cela sveta.<br/>
Vihor ustanka dunu jače,<br/>
<br/>
oživeše jame i vrtače,<br/>
iz svake zakopana puška planu,<br/>
odnese vihor krov od slame,<br/>
ispod krova se cev pokaza;<br/>
podiže jela zelen-dolame,<br/>
oružje stoji kraj šumskih staza;<br/>
odlubi se na starom hrastu kora,<br/>
ispod nje barut zamirisa;<br/>
isturi pušku voda iz zdenca,<br/>
i humka skora sina prvenca,<br/>
planuše pragovi, s njiva međaši:<br/>
– Oružje paši!<br/>
<br/>
Ustanak kao voda poplava<br/>
grunu svuda.<br/>
U toku jedne noći se zbiše<br/>
neviđena čuda:<br/>
<br/>
poraste dečak bez nausnica<br/>
u mladića,<br/>
stasaše orli granatih krila<br/>
od goluždravih u gnezdu ptića;<br/>
prestade mati<br/>
za poginulim sinom da plače,<br/>
poskida crne ubradače<br/>
kao da se kupe kićeni svati;<br/>
pođe u bunu i žena što joj<br/>
prvenče još u kolevci spava,<br/>
s unukom krete i ded da gine,<br/>
potekoše borci kao lava,<br/>
u toku jedne jedine noći<br/>
poniče šuma iz ledine.<br/>
Pođe ustanak brže nego<br/>
julske oluje,<br/>
preko dubokih voda bez broda,<br/>
preko ponora,<br/>
preko močvari<br/>
gde treset tinja,<br/>
preko pučina i pustinja.<br/>
<br/>
Grme zajedno, ječi gora,<br/>
stari, prastari džeferdari;<br/>
puška dedovska, duga grana,<br/>
u rodnom kraju iskovana;<br/>
dvocevka prosta<br/>
koja od oca sinu osta;<br/>
srmom kićene puške bele<br/>
kao stabljike pune cveta;<br/>
puške kojih se prvi put ruka<br/>
boraca maši:<br/>
– Oružje paši!<br/>
Glas se odazva iz zemlje cele,<br/>
iz cela sveta.<br/>
<br/>
(1952)<br/>

