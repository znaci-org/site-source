<h2>II verzija</h2>

<pre>
Oj Moravo, reko naša,
Zašto plačeš ti?

Što pope — što popevaš, tužnu pesmu,
Pesmu žalosti?

Usahla je pesma naša, 5
Čujte Srbi svi,

Jer se na — jer se našom zemljom liju,
Potoci krvi.

Fašističke horde čuvaju,
Obale moje, 10

Da ja ne — da ja ne bi prevozila
Sinove svoje.

Partizani, heroji moji,
Pođite u boj,

Za slobo — za slobodu svog naroda 15
Dajte život svoj.

Opet će se zaoriti,
Pesma radosti:

— Oj Moravo, moje selo ravno.
</pre>

<p>(Sergije Dimitrijević, Crnotravske i leskovačke narodne pesme oslobodilačkog rata i revolucije, Beograd, 1967., str. 80.)</p>

<h2>III verzija (Kako ne bi ja pevala)</h2>

<pre>
Kako ne bi ja pevala,
Pesmu žalosti,
Kad po na, kad po našoj zemlji svuda,
Gaze fašisti.
</pre>

<p>(Sergije Dimitrijević, Crnotravske i leskovačke narodne pesme oslobodilačkog rata i revolucije, Beograd, 1967., str. 80.)</p>