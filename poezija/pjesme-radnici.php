<h1>Vladimir Popović - Pjesme radnici</h1>

I<br />
<br />
Ljubim te, ženo, išibana bijedom,<br />
prezrena ženo od otmjenog svijeta.<br />
Neću ti pjevat o mjesecu blijedom,<br />
kosi crnoj, o ljepoti cvijeta.<br />
<br />
Jer znam: u tvojoj duši nema čežnja<br />
što žude ljubav i osmjehe neba;<br />
strt svijet je tvoj romantičnih težnja,<br />
svagdanjom brigom za komadom hljeba.<br />
<br />
Ja bijah mlad kad spoznah moć laži,<br />
kojoj samo još skeptici dvoje.<br />
O, robinjo ženo, kroz pjesme sad moje<br />
današnji život riječ istine traži.<br />
<br />
Ljubim te ženo, išibana bijedom,<br />
u društvu u kom si u cijeni smeća.<br />
Ne pjevam ti stog o mjesecu blijedom,<br />
kosi crnoj, o ljepoti cvijeća.<br />
<br />
II<br />
<br />
