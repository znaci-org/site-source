  Pesma je nastala u februaru 1944. godine u kampu na Križnoj gori iznad Cola. Dan po dolasku, Lev Svetek-Zorin popeo se na obližnji vrh, odakle je gledao Primorsku. Iste večeri na mitingu je čitao svježe pisanu Vstalu Primorsku. Pjesma se proširila zbog talijanskog fašizma i ugnjetavanja Slovenaca Primorja, koje je nakon prvog svetskog rata pripalo Italiji. Uglazbio ju je Rado Simoniti 1968. za proslavu 25. obljetnice opće Primorske bune nakon kapitulacije Italije u jesen 1943. godine.
