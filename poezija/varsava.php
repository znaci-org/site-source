<h1>Desanka Maksimović - Varšava</h1>

Hteli ste da nema Varšave,<br/>
da zazidate na njoj vrata;<br/>
hteli ste da nikne trava,<br/>
da se gušteri i guje kote<br/>
iznad vekovnog ljudskog znoja,<br/>
iznad ljudskih najdubljih prava,<br/>
iznad slobode i lepote.<br/>
<br/>
Varšavu vi takli niste.<br/>
Iznad napaćenog, srušenog grada,<br/>
potoka krvi, otrovnih rana,<br/>
iznad paljevina i iznad jama<br/>
stoji ona opet sazidana,<br/>
puna zgrada<br/>
koje zida sloboda sama.<br/>
<br/>
Varšavu vi ni takli niste,<br/>
niste je satrli rata gromom;<br/>
ona se diže u visine<br/>
od kojih vrtoglavica narode hvata;<br/>
nad svakim njenim srušenim domom<br/>
penje se svetao sprat iznad sprata,<br/>
slobode kruna.<br/>
Varšava živi, Poljaka puna.<br/>
<br/>
Varšavu vi ni takli niste,<br/>
nije ona mrtva ni ukleta,<br/>
ni potonula u moru mraka.<br/>
Krilata i puna sjaja<br/>
izletela je iz ruku Poljaka<br/>
i grad je sada celoga sveta,<br/>
sad ima stotinu zavičaja.<br/>
<br/>
Hteli ste da Varšave nema,<br/>
da se sa zemljom sravni i satre;<br/>
mesto nje da budu puste ravni<br/>
pune blata;<br/>
a Varšavu vi ni takli niste,<br/>
ona vas lepotom svojom čika:<br/>
viša je od Snežnice i od Tatre,<br/>
nesalomljivija od Karpata<br/>
i prostranija od Baltika.<br/>
