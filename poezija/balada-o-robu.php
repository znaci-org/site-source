<h1>Oskar Davičo - Balada o robu</h1>

Ako se od gneva dimim, suv od tame,<br/>
prsa moja vatra davno već ne zgreja,<br/>
ako sam tužio, posle suza za me<br/>
nikad nije bilo ni mrvice smeja.<br/>
Ako rano ranim, ja bdim u tri smeme,<br/>
kraj mene prolaze osmesi ko duge,<br/>
ako se s proleća višnje zarumene,<br/>
ja ću ih želeti — za druge, za druge.<br/>
Ako sam s brnjicom brao preko lica<br/>
tamničaru grožđe, a jeseni — dunju,<br/>
jamu dublju kopam no što je grobnica<br/>
tešku jamu kopam, a tuga će u nju.<br/>
<br/>
Ako sam kroz prste pušto dan i bure,<br/>
ja nisam kotvio na nedrima luke,<br/>
ako sam žurio kao da me jure,<br/>
zadržale nisu ništa moje ruke.<br/>
Ako mi je lik crn, pogled križni,<br/>
ja ne vidim, braćo, sleme dušosklona.<br/>
Znam, spori će stići, a ja, nedostižni,<br/>
pod suncem u cvetu ostaću sii’oma.<br/>
Ako ne dočeko smeh koji je bol reko,<br/>
spalite mi telo i u njemu sumnju.<br/>
Jamu dublju kopam, ići je daleko,<br/>
tešku jamu kopam, a suze će u nju.<br/>
<br/>
Ako nisam nikad ležao na mekom,<br/>
ja sam sa pticama slobodom letio,<br/>
ako znam gospoje da se peru mlekom,<br/>
otkad srpski zborim, ja ga nisam pio.<br/>
Ako noćas umrem, neće me crv grobar<br/>
upitati: — Druže, čemu tebi lanci?<br/>
Crv će me zglodati, crv, podzemni sobar,<br/>
Pomešće me: šta će u podzemlju stranci?<br/>
Braćo, šta ću dati? Ašov kosti stare?<br/>
A duša bi htjela da zagrli munju.<br/>
Jamu iskopajte, o ruke krvave!<br/>
— Dublju jamu kopam, a lanci će u nju.<br/>
<br/>
Ako su mi živom zapalili sveće,<br/>
nek sad sunce spali smrt u mome trunju,<br/>
jamu sam iskopo, nije bilo veće...<br/>
Tamnu jamu kopam, a užas će u nju.<br/>
