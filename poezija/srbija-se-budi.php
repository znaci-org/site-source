<h1>Desanka Maksimović - Srbija se budi</h1>

Noć se hvata.<br />
Srbija se budi, postaje ona stara;<br />
šapat ide od jaruga do pojata,<br />
od kamena<br />
do pastirskog plamena.<br />
Daljine svetle od zvezda i vatara.<br />
<br />
Ljudi u tami postaju<br />
opet strašni i mrki;<br />
davnašnja dela opet se zbivaju;<br />
namernici svu noć na prozor kucaju,<br />
pogledom samo boga nazivaju;<br />
oko pri drhtavoj sveći sene,<br />
puška plane.<br />
Hitro: skoro će da svane.<br />
<br />
Nad Srbijom noć se hvata,<br />
noć, čas dobrog zaborava;<br />
prljava koprena se sa Srbije skida;<br />
sijaju šume od svetlih uspomena,<br />
nema više poraza ni stida:<br />
u svakom pazuhu brda,<br />
u gnezdu krstina i sena<br />
po junak spava.<br />
<br />
Konji svu noć kasaju,<br />
uši radosno čulje:<br />
čuju se odasvud junački krici,<br />
odasvud glasnik hoda;<br />
putevi su opet beli<br />
kao da su njima jezdili<br />
samo ustanički konjanici.<br />
<br />
Srbija se budi,<br />
životom viteškim opet živi<br />
od sutona do zore.<br />
Svi koji su za smrt njenu bili krivi,<br />
koji nisu pali<br />
na njenom pragu,<br />
postaju opet ljudi<br />
spiraju sa imena ljagu.<br />
<br />
Srbija se opet budi:<br />
sve što se začinje<br />
u napaćenoj utrobi žena,<br />
sve što niče iz misli<br />
i semena,<br />
sve što tlu Srbije<br />
živi i rodi<br />
namenjuje se slobodi.<br />
