<h2>II verzija</h2>

Ustaj, seljo, ustaj, rode,<br />
Bez oblaka nema vode,<br />
<br />
Bez oblaka nema vode,<br />
Ni života bez slobode.<br />
<br />
Nema boja bez junaka,<br />
Ni pobjede bez pušaka.<br />
<br />
Kad je srca u junaka,<br />
Biće boja i pušaka.<br />

<p>(Narodne pjesme Korduna, sakupio i uredio Stanko Opačić-Ćanica, Zagreb: Prosvjeta, 1971, str. 217.)</p>

<h2>III verzija</h2>

Ustaj, seljo, ustaj rode,<br>
U narodni front slobode.<br>
<br>
U narodni front slobode,<br>
Da se braniš od gospode.<br>
<br>
Od popova mantijaša,<br>
I ostalih zelenaša.<br>
<br>
Od žandara i ustaša,<br>
I ostalih pendrekaša.<br>
<br>
Tvoja zemlja sad robuje,<br>
A fašista gospoduje.<br>
<br>
Ti ne budi meta njima,<br>
Hajd u šumu s drugovima.

<h2>IV verzija</h2>

Ustaj, seljo, ustaj, rode,<br />
U narodni front slobode,<br />
<br />
Ustaj sada il' nikada,<br />
Jer tvoj narod ljuto strada,<br />
<br />
Tvoja zemlja sad robuje,<br />
Fašizam ti gospoduje,<br />
<br />
Svi narodni izdajnici,<br />
Postaše mu saveznici,<br />
<br />
Mnogog sinka on ti pobi,<br />
A mnogog ti i zarobi,<br />
<br />
Te sad pište u tuđini,<br />
Okreću se domovini.<br />
<br />
Partizani borbu vode<br />
Da svoj narod oslobode.<br />

<p>(Sergije Dimitrijević, Crnotravske i leskovačke narodne pesme oslobodilačkog rata i revolucije, Beograd, 1967., str. 129.)</p>

<h2>V verzija</h2>

Ustaj, seljo, ustaj, rode,<br />
Da se braniš od gospode,<br />
<br />
Od gospode ministara,<br />
Nedićevca kokošara.<br />
<br />
Ustaj, seljo, ustaj, rode,<br />
Imovina sva ti ode,<br />
<br />
Od gospode zelenaša,<br />
