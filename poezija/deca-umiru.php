Smrt nije radosna nikad, ni kad prekida lanac patnji,
ni posle dugog života, kad čoveka umor svali;
hiljadu lica smrt ima, od podle do plemenite,
ali laka smrt nije nikad, ni kad ste je sami zvali.

Priroda ništi i stvara. Po njenim zakonima
sklanja se trošno, iživelo, pred bićima svežim, novim.
Život ne zamire ukočen, život stalno nanovo klija.
No čovek živi još i po suprotnostima čovekovim.

Gledali smo mlade sadove, gde cveta buduća raskoš,
gledali smo zasejana polja na kojima klija setva!
Pred nama je sad budućnost: mlada bića puna nade.
O, draga nevina lica! Da ih ne pokosi prerana žetva!

Gledali smo toliko puta kako umire malo dete.
Taj iznureni mali lik, te raširene oči od stakla.
To malo telo ispruženo posle zadnjih trzaja strave.
Te oči pitaju: zašto? Ledena ruka nas je takla.

Umiru stara stabla. Novi izdanci smenjuju šume.
Umiru starci. Bolesnici. Za ideju ginu borci u boju.

Ali zašto umiru deca? Zar moraju da umiru malena,
još dok ni koraknula nisu na životnu stazu svoju?

Mala slabašna tela, dojena usahlom sisom,
malo podrumsko cveće u bednim izbicama,
ginu i bez boja, kao leptirići u proletnjem mrazu,
i tužno mudar lik dobiju ona što preostanu sama.

Smrt dođe svačijem ocu, svačijoj majci, svakom redom —
al’ moraju li umirati deca, pokošena kao pupoljci bedom?

