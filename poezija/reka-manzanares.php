<h1>Čedomir Minderović - Reka Manzanares</h1>

<pre>
Čuješ li: šumi i krči, propinje se i ključa.
Na leđima talasa nosi leševe i crnu penu.
U nemoćnom, divljem besu nadima se i klobuča
I raste i raste i zapljuskuje zemlju raskrvavljenu.

Možda su mostovi kasno dignuti u vazduh? Ili
Možda su borci pali pred njenim obalama?
Manzanares! Manzanares! Tebe su pregazili!
Pregazili teškim krvavim cokulama!

Čuješ li, daleki druže sa Temze, sa Jancekjanga
Dok se umoran vraćaš kući u kasno veče
Čuješ li kako stupa krvava, crna falanga
Nad našom zgaženom rekom što celim svetom teče!

Falanga crna stupa. Pazi! Približuje se!
Manzanares, zgažena, krklja, valja se i huči:
— Umorni celog sveta! Ujedinite se!
Još nije, još nije kasno! Okovi moraju pući!

Zasrebriću se jednog dana i poći u daljine
Kroz plodna žitna polja. Beskrajno daleka.
Danas — na obalama kojim borac za borcem gine
Sutra — preoraće ih traktor oslobođenog čoveka!
</pre>

