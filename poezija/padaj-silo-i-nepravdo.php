<h1>Padaj silo i nepravdo</h1>

<p>(nastala na osnovu pjesme "Slobodarka" Josipa Smodlake)</p>

Padaj, silo i nepravdo,<br/>
narod ti je sudit zvan;<br/>
bjež'te od nas noćne tmine,<br/>
svanuo je i naš dan.<br/>
<br/>
Pravo naše ugrabljeno<br/>
amo natrag dajte nam,<br/>
ne date li, ne molimo,<br/>
uzet će ga narod sam.<br/>
<br/>
Gradove smo vam podigli,<br/>
turne, kule gradili,<br/>
od uvijek smo roblje bili<br/>
i za vas smo radili.<br/>
<br/>
Nevolja će biti vela<br/>
po palacim tvrdimi,<br/>
kad vidite da sa sela<br/>
s maškinima gremo mi.<br/>
<br/>
Nastati će novo doba<br/>
Matija Ivaniću,<br/>
ustati ćeš tad iz groba,<br/>
s tobom u boj poći ću.<br/>
<br/>
Zastava će nova viti,<br/>
iznad naših glava tad;<br/>
radnik, seljak jedno biti,<br/>
isti im je trud i rad.<br/>

