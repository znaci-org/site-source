Nad grobom borca Radoja<br/>
bijela breza samuje,<br/>
srebrna breza tuguje.<br/>
Kraj groba mladog Radoja,<br/>
bez staze i bez bogaze,<br/>
u noći četa putuje.<br/>
<br/>
I zbori momak Velimir,<br/>
udarne čete komandir:<br/>
<br/>
Ej, lakše, momci, gazite,<br/>
nek modra spava tišina,<br/>
evo već puna godina,<br/>
puče mi sablja nadvoje:<br/>
tu nam je pao Radoje.<br/>
<br/>
U noći breza šumori,<br/>
Radoje četi govori:<br/>
<br/>
»Družino, vjerna družino,<br/>
prošli ste brda, doline,<br/>
puca li puška slobodna,<br/>
gori li buna narodna?«<br/>
<br/>
»Radoje, druže Radoje,<br/>
kad voda krene niz goru,<br/>
niko je nazad ne vrati,<br/>
što jače vjetar udara,<br/>
više se oganj razgara.<br/>
<br/>
Krvavo kolo počelo,<br/>
da nikad više ne stane,<br/>
dokle nam jutro ne svane.<br/>
<br/>
»Družino, stara družino,<br/>
vi, momci lica vesela,<br/>
skoro ste otud iz sela,<br/>
jeste I mi majku vidjeli?«<br/>
<br/>
»Kada je tuđin upao,<br/>
s nama je stara krenula;<br/>
tri dana goru gazila,<br/>
ranjena druga vodila,<br/>
al ipak nije sustala.<br/>
Radoje, druže Radoje,<br/>
majka je heroj postala.«<br/>
<br/>
»Recite, stari drugovi,<br/>
gazeći goru zelenu,<br/>
vidjeste l moju Nevenu?«<br/>
<br/>
»U našem zadnjem proboju,<br/>
kod brze rijeke zelene,<br/>
nestade lijepe Nevene.<br/>
U gustoj travi kraj vode,<br/>
krvava osta marama,<br/>
poruka zadnja u boju:<br/>
"Moj zadnji pozdrav Radoju!"<br/>
<br/>
»Oj, četo, gujo s prisoja,<br/>
živi li otac Ilija,<br/>
žali li sina Radđoja?«<br/>
<br/>
»Starog ti oca svi znadu,<br/>
i on je s nama pošao,<br/>
stigao stari brigadu.<br/>
Nekad je tebe imao,<br/>
sad ima djece hiljadu,<br/>
čelične djece iz boja,<br/>
zamijeniše mu Radoja.<br/>
Studena kuća planina,<br/>
ali se ne da starina.«<br/>
<br/>
Trepetom breza zasija,<br/>
pali se junak nasmija.<br/>
<br/>
Družina dalje putuje,<br/>
iščeznu četa u gori.<br/>
<br/>
Nad grobom breza šumori,<br/>
bijela djeva tanana,<br/>
šapuće breza dragana,<br/>
ognjene sanja svatove<br/>
i barjaktara Pobjedu,<br/>
razvio zoru rumenu.<br/>
<br/>
(1945)<br/>

