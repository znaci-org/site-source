<h1>Vladimir Popović - Mjesec i partizani</h1>

Legosmo na travu — poslije bitke one —<br/>
a vrela ko vrela, dokona, romone.<br/>
<br/>
Sva u zlatu, s grane, odbi se još vuga:<br/>
strah je ljudi... Umor... Ni radost ni tuga.<br/>
<br/>
Puške strše. Sija nebo u opalu<br/>
i mirisi smilja prolaze u valu.<br/>
<br/>
Netko glavom klonu na jastuk od torbe:<br/>
što li junak sniva?... Možda nove borbe.<br/>
<br/>
Al kada se mjesec probi povrh luga,<br/>
iz sprdnje mu netko stao da se ruga:<br/>
<br/>
"Sve si bljeđi, kume, upalo ti lice,<br/>
i uštap ti sliči na kljun grabljivice."<br/>
<br/>
On nas, gorak, gleda, i tužno se smiješi;<br/>
sluti: nije vrijeme da tepa i tješi.<br/>
<br/>
A onda se trže; zvuk gorama ječi<br/>
dok na žuta usta kuljaju mu riječi:<br/>
<br/>
„Ne snivate ni vi moma oči sane:<br/>
zašto puče život u rake i rane?!<br/>
<br/>
kakva crna bijeda vas goni od kuća?<br/>
to ćete u šumi, djeco, do svanuća?!<br/>
<br/>
A kakva je zemlja ...! Poznati je nije.<br/>
Čemu se ta vatra sa prisoja vije?<br/>
<br/>
Tko poravni život!?... Nigdje jedne sjene<br/>
kuće i kolibe mahom pougljene.<br/>
<br/>
Nisam više ni ja pogača od srme;<br/>
ja putujem s vama i kad puške grme.<br/>
<br/>
Ponekad se zbunim: često usred noći<br/>
plane mirno selo. Dim grize mi oči.<br/>
<br/>
Ja svu svjetlost pustim da otkrije šta je:<br/>
al' sa zemlje ni pas na mene ne laje.<br/>
<br/>
Tek poneki metak put neba se vine;<br/>
zar krvolok hoće i mene da skine?<br/>
<br/>
Ja zadrhtim, bolan, znam i uzrok činu:<br/>
mrak je staza onih što na svijetlu ginu..<br/>
<br/>
I on, gorak, gleda trudne partizane<br/>
i vuga još jedna odbi se od grane.<br/>
