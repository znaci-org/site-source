<p>Sve je crno. Budan Stari.<br>
nad kanjonom bdi u noći,<br>
misli brižan i stražari..<br>
iz obruča kako proći?</p>

<p>Nov most! Mi na drugoj strani.<br>
Srušićemo stari, reko,<br>
neka misle: partizani<br>
