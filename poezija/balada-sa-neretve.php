diži se i dalje kreni,</p>

<p>jer noć ova život vredi…<br>
Svi da prođu, Stari čeka,<br>
od brige mu kosa sedi.<br>
Pobeđena huči reka.</p>

<p>Život se od smrti deli.<br>
