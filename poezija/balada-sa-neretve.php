<p>Iza leđa zlotvor goni,<br>
preko vode zlotvor vreba.<br>
Avioni… avioni…<br>
seju pomor s belog neba.</p>

<p>Nigde puta, a sneg veje,<br>
na kamenu mrtvo dete.<br>
- Zemljo, reci, pravda gde je?<br>
