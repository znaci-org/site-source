Crna Travo, ti planinsko mesto,
U te beše reakcije često,
Reakcije bugarskih fašista,
Svako znade bilo je zaista,
Crnu Travu s zemljom poravniše, 
I sve redom vatrom popališe,
Kad je svaka reakcija bila,
Komandanta Brku je tražila,
I ostale naše partizane,
Što nas brane u najcrnje dane. 
Crna Trava suviše pasivna,
A za borbu postala je divna.
Niko nije izgubijo nadu,
Partizansku slušao komandu.
I radio iz Moskvu nam kaže: 
— Okupator da se ne pomaže. —
Crna Trava uvek vesti znala,
Za svo vreme radio čuvala.
Uz radio drugar Stevo piše,
Trećeg dana vesti izlaziše. 
I na zboru Smajević nam veli:
— Evo braćo idu dani beli.
Nemcima je sada prošla sila,
Rusija ih natrag povratila. —
I docnije drug Baja nam stiže 
I ustanak pravi on podiže.
Omladina sva na zboru dođe,
Dobrovoljno sva u borbu pođe.
A i odbor za vojsku se brine,
Da imade toplije haljine. 
Dobrovoljno obuće se nosi,
Samo borci da ne idu bosi.
A devojke, naše drugarice,
Za drugari pletu rukavice.
I džemlera svaki vojnik ima, 
Svakom drugu da ne bude zima,
Crna Trava među sobom veli:
— I poslednji zalog da se deli! —
Crna Trava malo hrane ima,
Al' što ima, ona deli svima. 
Ona uvek sluša svoje vođe,
Dok fašizmu crni danak dođe.
Zbog toga se mnoga žrtva dala,
Dok je vlada fašistička pala,
Bugarskoga Bogdana Filova, 
I fašiste Dobri Božilova.
Dok ne dođe jedna nova vlada:
Iz naroda izašla je sada.
To je vlada Slavča i drugova,
I pošteni bugarskih sinova, 
Koji žele bratstvo sa Srbiju,
Među sebe krvi da ne liju.
Neće biti ubistva odsade,
Nema više fašističke vlade.
Kad je Slavče kod nas dolazio, 
Tad je Slavče nama govorio,
Da nam preda bugarske krvnike
Što naš narod stavljaše na muke.
To su bili ponajgori ljudi,
Al im zato narod danas sudi. 
</pre>

<p>(Sergije Dimitrijević, Crnotravske i leskovačke narodne pesme oslobodilačkog rata i revolucije, Beograd, 1967., str. 92-93.)</p>