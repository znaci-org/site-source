<h1>Branko Ćopić - Lekcije iz zemljopisa</h1>

<p>Poslije godina ratnih<br>
u rodni dođoh kraj.<br>
Drumovi… Potok i njive…<br>
Voljeni zavičaj…</p>

<p>Osnovna škola pod gajem ćuti,<br>
slika odavna znana,<br>
mnogo je puta gađana topom,<br>
često bombardovana.</p>

<p>Uđem u razred…<br>
Tišina šumi…<br>
Slike po zidu bijelom…<br>
U ćošku tabla s parolom bojnom<br>
i globus – ranjen šrapnelom.</p>

<p>Evo i stare računaljke,<br>
tu sam učio ja:<br>
četiri kruške plus dvije kruške<br>
i mudro dvaput dva.</p>

<p>Redovi klupa. Ej, tugo moja!<br>
Nema drugova znanih!<br>
Bezbroj imena po njima ćuti,<br>
nožićem urezanih.</p>

<p>Otišli su mi drugovi davno<br>
u bojne bataljone,<br>
a ovdje, evo, kao da i sad<br>
njihove riječi zvone.</p>

<p>Čitam imena…<br>
Na jednoj klupi<br>
urezan zapis sa strane:<br>
„Ovdje je nekad učio školu<br>
dječak Desnica Brane“.</p>

<p>Sjećam se dobro garavog Brane,<br>
junak, srce viteško,<br>
sve mu je išlo,<br>
ali zemljopis –<br>
to je učio teško.</p>

<p>Recimo, riječ je o Grmeču,<br>
kroz prozor on se plavi,<br>
učitelj pita,<br>
a Brane, zbunjen,<br>
dugo se češka po glavi:</p>

<p>„Grmeč planina… Grmeč… on je…“<br>
kradom se knjige laća –<br>
„on se prostire…<br>
molim, on se…<br>
od Ključa do Bihaća.</p>

<p>On je šumovit… drvo… on je…“ –<br>
i pero vrti u ruci –<br>
„po njemu rastu borovi, jele,<br>
i s njima – medvjed i vuci…“</p>

<p>Učitelj gleda dječaka tužno.<br>
„Ovo ti najbolje nije!“<br>
A Brane sjeda, crven i zbunjen,<br>
i od nas oči krije…</p>

<p>Davno je bilo…<br>
S tugom se sjećam<br>
djetinjskih dana zlatnih…<br>
Jednog ih dana<br>
s tutnjavom zbrisa<br>
požar godina ratnih.</p>

<p>Pođosmo i mi za drugom Titom,<br>
skoči Krajina ljuta.<br>
U žaru boja dadosmo zavjet:<br>
„Nikad s Titova puta!“</p>

<p>Drugovi moji iz školskih klupa<br>
slušam –<br>
dobro se biju,<br>
najviše čujem za mitraljesca,<br>
za Branu nišandžiju.</p>

<p>Tuk’o je švapsku diviziju.<br>
„Princ Eugen“ se zove,<br>
gađao bunker,<br>
na juriš iš’o<br>
kod Bukve Okanove…</p>

<p>Četvrta, zimska ofanziva,<br>
valja se poput džina:<br>
u noći sniježnoj mitraljez laje,<br>
iz magle grmi mina.</p>

<p>Grmeč…<br>
Bolnica otstupa žurno…<br>
Na čuki – bitka prava:<br>
to Brane štiti ranjene borce<br>
i Švabe zadržava.</p>

<p>Juriš po juriš…<br>
Ranjeni Brane<br>
uporno glavu diže,<br>
nišani mirno:<br>
planina ječi<br>
i zrno zrno stiže.</p>

<p>U ranu zoru stigosmo i mi.<br>
Mraz je stezao ljuti.<br>
Po snijegu Švabe bezbrojne leže,<br>
na čuki – mitraljez ćuti.</p>

<p>Nađosmo Branu:<br>
u krvi leži,<br>
umorno glava pala,<br>
a ruka čvrsto mitraljez drži –<br>
Švabe je zadržala.</p>

<p>Kod njega nađoh bilježnicu,<br>
Branin rukopis teški:<br>
kvrgava slova,<br>
redovi krivi<br>
i bezbroj – more greški.<br>
„Mitraljez“- piše velikim slovom,<br>
a „Grmeč“ – opet, malim,<br>
„Švabesu“ – piše sastavljeno,<br>
zatim: „Rafalom Palim“.</p>

<p>Listam po svesci.<br>
odjednom, evo,<br>
čudnovat naslov sine,<br>
pisano krupno i pažljivo:<br>
„Opis Grmeč Planine“.</p>

<p>„Grmeč se stere dva dana hoda,<br>
gora i goli kam,<br>
dvaput ga prođoh sa brigadom,<br>
a sedam puta sam.</p>

<p>On je bezvodan –<br>
i to piše –<br>
svakom dajem do znanja,<br>
tamo sam često kišnicu pio<br>
iz kakvog trulog panja.</p>

<p>Podosta ima skrovitih mjesta<br>
za mnoge bolnice naše,<br>
za borbu ima prekrasnih čuka,<br>
toga se Švabe plaše.</p>

<p>S ljeve mu strane Sanica selo,<br>
ustaški „Alkazar“,<br>
na nj smo krenuli na juriš, noću,<br>
pao je – jasna stvar.</p>

<p>Sdesna je blizu Bihać stari,<br>
tvrđava ponosita,<br>
zauzesmo ga novembra petog,<br>
tu sam vidio Tita.</p>

<p>Bogat je Grmeč,<br>
zapamti druže,<br>
drveta – prave strijele.<br>
U nizi rastu krošnjate bukve,<br>
a malo više – jele“.</p>

<p>Na kraju piše:<br>
„Rođena goro,<br>
tuđin juriša na te,<br>
a ja te branim do zadnjeg metka<br>
i daću život za te“.</p>

<p>Zasuzi oko…<br>
I tiho kažem:<br>
„O Brane, druže mio,<br>
ti si u žaru narodnog boja<br>
zemljopis naučio.</p>

<p>Kako si krasno lekciju znao,<br>
da ti se divi svijet!<br>
U ime naše divizije<br>
dajem ti ocjenu „pet“!</p>

<p>Istina, bješe grešaka dosta,<br>
koje ti članak krase,<br>
a ja ti ipak „o d l i č n o“ dajem,<br>
a greške primam na se“.</p>

<p>Sahranismo ga pod prvom jelom,<br>
gorom plotun zaječa.<br>
I tako Brane vječito osta<br>
na straži sred Grmeča…</p>

<p>U školi stojim<br>
i s mnogo tuge<br>
čitam slova sa strane:<br>
„Ovdje je nekad učio školu<br>
dječak Desnica Brane“.</p>

