Bijela je klikovala vila,<br />
Sa Radana, visoke planine,<br />
Pa doziva dole u ravnine,<br />
Pobratima Ljubića Stojana.<br />
Bjela vila njemu progovara:<br />
— Čuješ, Stole, ti moj brate mili,<br />
Ta Dražina banda sad se sili,<br />
Pet stotina izroda i više,<br />
Kosančiću sad se uputiše.<br />
Konje jašu i pesme pevaju,<br />
Jablanici nevolje spremaju. —<br />
Kad je Stole vilu razumio,<br />
Na noge je junačke skočio,<br />
Te brigadu drugu okupio,<br />
Pa im onda riječ govorio:<br />
— Čujte mene, svi drugovi moji,<br />
Dobro znate gde Kosančić stoji.<br />
Tamo nama valja putovati,<br />
S četnicima junački se klati,<br />
Uništiti plaćene izrode,<br />
Radi naše zlaćene slobode.<br />
Partizani ne mož’mo se zvati,<br />
A naš narod da cvili i pati. —<br />
Kad drugovi njega razumeše,<br />
Ko orlovi suri poleteše,<br />
Na njima su zvezde petokrake,<br />
Pa da vidiš te naše junake.<br />
Kosančiću kada dopadoše,<br />
Blokirati odma ga stadoše.<br />
Kad je Stole društvo uredio,<br />
Družini je svojoj govorio:<br />
— Oj, drugovi, puške ne palite,<br />
Već u selo izrode pustite,<br />
Bugarski ih avioni prate,<br />
Ali zato oni će da plate. —<br />
Kada naši vatru oboriše,<br />
Tu se dvije vatre ukrstiše,<br />
Mili Bože, na svemu ti hvala,<br />
Da je kome stati pa gledati,<br />
I ušima svojim poslušati,<br />
Kad nastade vatra iz pušaka,<br />
Jaukanje ranjenih junaka,<br />
Avioni biju iz oblaka,<br />
Svega toga naši se ne plaše,<br />
Već i njima oni otpor daše.<br />
A uveče oko deset sati,<br />
Ljubić Stole stade jurišati:<br />
— Samo napred, sokolovi moji,<br />
Danas nama pobeda predstoji. —<br />
Puna kuća tih izroda beše,<br />
I na kuću oni poleteše.<br />
Naš je Stole međ' prvima bio,<br />
Dražinovce mnoge oborio.<br />
U borbi ga jedno zrno zgodi,<br />
Mitraljez ga četnički pogodi.<br />
Ranjen Stole ne cvili, ne ječi,<br />
No izusti nama ove reči:<br />
— Borite se, drugovi i rode,<br />
Do pobede i zlatne slobode! —<br />
To izusti, pa dušu ispusti.<br />
Kad drugovi reči razabraše,<br />
I mrtvoga junaka videše,<br />
Na četcike juriš učiniše,<br />
Sto pedeset izroda ubiše,<br />
Svo oružje njino zapleniše.<br />
Ubiše nam osam partizana,<br />
Meću njima Ljubića Stojana,<br />
Njega narod zaboravit neće,<br />
Dok se sunce po nebesa kreće.<br />

<p>(Sergije Dimitrijević, Crnotravske i leskovačke narodne pesme oslobodilačkog rata i revolucije, Beograd, 1967., str. 81-82.)</p>

