<h1>Desanka Maksimović - Siromahov grob</h1>

Otac mi leži u zapuštenom groblja kraju,<br/>
gde prolazi su blatnjavi i krivi,<br/>
gde na srcu pokojnika<br/>
samo visoka trava živi.<br/>
<br/>
Dole u centru blešte spomenici,<br/>
natpis ih u stihovima kiti.<br/>
Grobarke iz crvenih kanti<br/>
cveće zalivaju.<br/>
I čoveku se čini<br/>
da oni što su u životu<br/>
bili zadovoljni i siti<br/>
i posle smrti uživaju.<br/>
<br/>
Ja još nisam ocu spomenik sagradio,<br/>
a isto mi ga je kao da sam bogat žao;<br/>
