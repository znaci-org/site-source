<h1>Branko Ćopić - Mali heroji</h1>

Evo junaka malog<br/>
u strašnom ratnome viru.<br/>
Ti se dosjećaš, druže:<br/>
pričam o pioniru.<br/>
<br/>
Ovakvih boraca malih<br/>
bile su pune brigade.<br/>
Ja ću o jednom da pričam,<br/>
o jednom — od hiljade.<br/>
<br/>
Putuje bataljon slavni,<br/>
kolona — trista drugova,<br/>
a tamo, sasvim pozadi,<br/>
maršuje jedan čova:<br/>
od puške veći nije,<br/>
na glavi partizanka,<br/>
opasač sa dvije bombe<br/>
i puška »talijanka«.<br/>
<br/>
To je partizan Mišo;<br/>
trinaest ljeta ima,<br/>
al ipak pušku nosi —<br/>
mio je drugar svima.<br/>
Lani je kurir bio<br/>
kod druga Koste Nađa,<br/>
a danas, već je borac,<br/>
iz puške sjajno gađa.<br/>
<br/>
Obje mu sestre borci,<br/>
Borka i mlada Mica.<br/>
U našoj vojsci ima<br/>
ratnika-drugarica...<br/>
........<br/>
<br/>
Sutjeska. Krvavi proboj,<br/>
mitraljez oštro briše.<br/>
Na juriš bataljon kreće.<br/>
Evo našega Miše!<br/>
<br/>
Naprijed je heroj Sava,<br/>
uzor svakome borcu,<br/>
za njim juriša Mišo,<br/>
pomaže Crnogorcu.<br/>
<br/>
Švapski mitraljez bije,<br/>
pogibe heroj Sava,<br/>
do njega pade Mišo,<br/>
klonu djetinja glava...<br/>
<br/>
Slomljen je švapski obruč,<br/>
dalje se prenosi boj,<br/>
pionir mrtav leži<br/>
na zemlji rođenoj.<br/>
<br/>
Krv se lagano sliva,<br/>
sprženu boji travu,<br/>
s gore se javlja vjetar,<br/>
miluje kosicu plavu.<br/>
<br/>
Na zemlji mališan leži,<br/>
smrt ga pokosi teška.<br/>
Čuju se topovi naši,<br/>
mrtav se Mišo smješka ...<br/>
<br/>
A sestre dalje jure,<br/>
strijele odapete.<br/>
Borka i mlada Mica<br/>
paloga brata svete.<br/>
