<h1>Gvido  Tartalja - Banovići</h1>

Dizalice crna još tog crnog zlata<br/>
da zahvatiš čvrsto u crne čeljusti!<br/>
Stegni ga! Otvori k’o gvozdena vrata<br/>
i k’o plod u jesen sa drveta spusti.<br/>
<br/>
Putovaće basen: k’o da reka teče.<br/>
Crna reka uglja prugom u daljine.<br/>
Teći će k’o vode Tinje, vode Spreče<br/>
kroz stoletne šume u žitne doline.<br/>
<br/>
Krelijusi buše. Raspada ce stena.<br/>
Gle, pod stenjem svuda crno cveće niče.<br/>
— Šlipere ovamo, u srce basena<br/>
i mišiće tvoje, mladi udarniče!<br/>
<br/>
Vojvođanska polja zlatnim rodom rode:<br/>
talasa se žito kao more sinje.<br/>
Prugom će poteći žito kao vode<br/>
talasave plave Spreče, plave Tinje.<br/>
<br/>
Bušilice buše. Dizalice stežu.<br/>
Pod bakarnom rukom gvozden alat zveči.<br/>
Dan zanose širi k’o ribarsku mrežu<br/>
i u pesmu slaže polete i reči.<br/>
<br/>
— Još uglja! Još žita! Plamti srce Bosne<br/>
k'o da cela šuma od radosti gori:<br/>
da sve dane jadne, sirotne i posne<br/>
mađijom u dane praznične pretvori.<br/>

