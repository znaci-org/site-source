<p>Mnoge revolucionarne pjesme spjevane su u kaznionicama za političke zatvorenike u Bileći, Sremskoj Mitrovici i
  Lepoglavi. Zbog uvjeta u kojima su nastale, ove pjesme se popularno nazivaju robijaškim. Nastajale su spontano i
  najčešće im se ne može odrediti autor, jer je u pisanju teksta učestvovalo više zatvorenika, a katkada cijela ćelija ili paviljon.</p>

<p>U kaznioni u Sremskoj Mitrovici je 1936. godine organiziran Prvi festival originalnih revolucionarnih pjesama na
  kojem su učestvovali svi sobni kolektivi. Od većeg broja konkurenata pobijedila je slovenačka grupa sa pjesmom
  Žetelice (kasnije preimenovana u Nabrusimo kose), posebnu nagradu žirija dobila je Mitrovčanka, a istom prilikom je nastala i Pjesma o Matiji Gupcu. </p>

