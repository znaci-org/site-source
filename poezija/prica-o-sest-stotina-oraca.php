a ljetos će se zanjihati žita<br />
ko vojska ponosita našeg Tita.<br />
<br />
U Knešpolju, pred zemljom siroticom, brko je obasjan stao,<br />
i nisko se zemlji poklonio,<br />
i suzu ubrisao.<br />
<br />
Majčice naša, nijema, zaleđena,<br />
iz kraja daleka dohodimo,<br />
znojem seljačkim čudotvornim da tebe oživimo<br />
i zrnom, vječito živim, oplodimo.<br />
<br />
Svečan i podmlađen, brko u šake pljuje<br />
i prvu masnu brazdu odvaljuje,<br />
za njim stotine ruku čvrsto ručice stežu<br />
