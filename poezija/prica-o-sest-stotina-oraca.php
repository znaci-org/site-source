<h1>Branko Ćopić - Priča o šest stotina orača</h1>

<p>
  <i>U proljeće, 1943, šest stotina seljaka iz novskog i prijedorskog sreza probili su se kroz neprijateljske straže zasjede i obradili opustošenu zemlju dubičkog sreza.</i>
</p>

Sijači nemili lani su domilili,<br />
izašli iz gvožda i iz betona,<br />
po našim selima i našim njivama<br />
oganj, i smrt, i korov su posijali<br />
sijači iz dubičkog garnizona.<br />
<br />
Ej, a sad je proljeće mlako raskvasalo,<br />
u svakoj mi pori diše,<br />
u svakom busu toplinom miriše.<br />
Ko će mi pustu čežnju da utiša?<br />
<br />
0j, tugo moja, gdje je britko ralo<br />
i širok molitven zamah sijača,<br />
kad zrnje, pljušti ko proljetna kiša.<br />
Kozaro pitoma, hajdučico lijepa,<br />
što tuđa kuga nije pokupila,<br />
ti si ko melem uza se privila<br />
i u ljut čelik pretopila,<br />
a ja sam zemlja čežnjiva i slijepa,<br />
pa od jednoga do drugog uranka<br />
pištim i zovem djecu bez prestanka.<br />
<br />
Kroz noć se miče kolona, a kiša proljetna sipi<br />
i teške gunjeve moči, i kape, i šešire,<br />
iz kola pokislih ručice plugova vire,<br />
dok točak nepodmazan plašljivo cvili i škripi.<br />
<br />
Opanak, raskipio, ljepljivo blato mijesi,<br />
promiče žbunje mrko<br />
i nježno šapuće brko:<br />
<br />
Potegni još malo življe, riđušo moja rođena,<br />
kraj Dubice nas čeka zemlja neobrađena,<br />
sanja brazde i sjeme, velikim pijanstvom diše,<br />
čujem joj šapat zanesen u šumu ove kiše.<br />
<br />
Pod kišom i tminom šesto seljaka orača<br />
