<br />
U brazde, kaplja po kaplja, mutan se rosi znoj,<br />
i zemlju osvaja teški orača seljački stroj.<br />
Nad njivom probuđenom vuku se mazglice tanke,<br />
pred oračima, napete i budne, stražare partizanke.<br />
<br />
(1944)<br />

