<h1>Desanka Maksimović - Osvajači</h1>

U običnom životu Nemci su, velite,<br/>
učtiva, uglađena bića,<br/>
nisu lopovi ni ubice,<br/>
puše lulu, piju pivo,<br/>
doručkuju masla i meda.<br/>
Možda je i tako,<br/>
ali što se to tiče nas,<br/>
kad je njihovo zajedničko,<br/>
stoglavo lice<br/>
puno krvi i užasa,<br/>
kad na sve tuđe gleda<br/>
ili nadmeno ili krivo.<br/>
<br/>
I, velite, rodoljubi su oni,<br/>
za domovinu bez razmišljanja ginu.<br/>
Neka je i tako,<br/>
ali naše rodoljube oni su pljuvali,<br/>
naše najsvetije borce<br/>
razbojnicima zvali,<br/>
ponizili nam zemlju do skota;<br/>
kad oni ubijaju, junaštvo to je,<br/>
kad mi svoj život branimo,<br/>
divljaštvo i sramota.<br/>
<br/>
I, velite, njihova deca su čista;<br/>
podsečenih noktića, umivenog lica<br/>
lepršaju kao cveće.<br/>
Verovatno je i tako,<br/>
ali što se to tiče nas,<br/>
zbog njih naša deca prljava,<br/>
i bez majki hode,<br/>
zbog njih kod nas teče<br/>
mesto pesme jauk i poklič buna,<br/>
i krv mesto peraće vode.<br/>
<br/>
I Nemica je, kažete, ljupka,<br/>
ima ženstvena nedra, okrugla,<br/>
i obraze rumene,<br/>
i besprekorna je mati.<br/>
Ali što se to tiče nas,<br/>
i ona je na naš narod<br/>
bacala rugla<br/>
i mirno sedela na kućnom pragu<br/>
dok sin njen negde progoni i strelja,<br/>
dok sav svet zbog zemlje njene pati.<br/>
I ona je našoj deci iz usta<br/>
zalogaj otela.<br/>
<br/>
Oni, velite, vole svoje ognjište,<br/>
svaki oko kuće gradinu ima,<br/>
i duž puteva zasađeno voće.<br/>
Ali u zemlji našoj<br/>
oni su ostavili zgarište,<br/>
useve pretvorili u barjake dima,<br/>
i kroz tišinu naših pitomih sela,<br/>
derali se užasni, užasno.<br/>
<br/>
I kad mislim na njinu viku,<br/>
na njine lule, pivo, decu, žene,<br/>
na dobroćudno lice im domaće,<br/>
voćke kraj puta zasađene,<br/>
ruke čiste kao od zlata,<br/>
dođe mi da vičem iz sveg grla:<br/>
živeli narodi prljavih nokata,<br/>
a srca čista;<br/>
živeli narodi što kod svoje kuće<br/>
psuju iz glasa,<br/>
a boje se na senku pravde da stanu;<br/>
živeli narodi čije žene nose<br/>
oružje iza pasa;<br/>
čija deca pupava hode;<br/>
živeli narodi čije opšte lice<br/>
blagom svetlošću blista!<br/>
<br/>
(objavljeno 1946)<br/>

