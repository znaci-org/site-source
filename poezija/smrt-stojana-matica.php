<h1>Smrt Stojana Matića</h1>

<p>(<i>Lička narodna pjesma</i>)</p>

<pre>
Zora snijegom svu krajinu bijeli,
kad pod Lapac momci doletjeli –

silni momci u dolinu grada,
pa ljut bojak zametnuli sada.

ne znaš, majko, ko li više vije;
il’ mećava, il’ puščane zmije.

a na čelu kobne bitke ove
vodi Stojan svoje sokolove.

takvog borca nigde bilo nije,
smrt ga gleda – on se na nju smije.

stisnu junak bogaze i pute,
smrtno mete dušmanine ljute.

kad sred boja, a u zoru ranu,
vidjeli smo nadu pokopanu. –

aoj, majko, sokola Stojana –
pade junak od golemih rana.

kad sloboda nasmija se gradu,
mrtvo telo predali smo hladu,

crnoj zemlji, ispod vitih jela,
posred Lapca, posred grada bjela.

plače gora, plače svaka grana,
nema nama Matića Stojana.
/što nam nema Matića Stojana/ 
</pre>

<h1>Izgubismo drug Stojana</h1>

<pre>
Baš u Lapcu jednog dana
dvajstsedmi februara
na srce nam pade rana
izgubismo drug Stojana.

S njim je palo još drugova
koje žali zemlja ova.
Pobiše ih izdajice
fašističke pristalice .

Drug Matića žali Lika
i u grobu on je dika
za narod je život dao
i u borbi mlađan pao.

Svjetal primjer Stojan dade
za slobodu junak pade.
Slava tebi drug Stojane
diko naša, partizane.
</pre>

<p>(Spjevala Boja Vojvodić. Pjeva se na melodiju: Na Kordunu grob do groba.)</p>

<h1>Pogibija Stojana Matića</h1>

<pre>
Dvadeset sedmi februar osvanu,
Vodi Matić četu odabranu,
Silni momci u dolini grada,
Pa ljut bojak zametnuli sada.
A na čelu kobne bitke ove
Vodi Stojan sive sokolove;

Takvog borca nigdje bilo nije:
Smrt ga gleda — on se na nju smije.
Kad sred boja, a u zoru ranu,
Vidjeli smo nadu pokopanu.
Aoj, majko, sokola Stojana:
Pade junak od golemih rana.
Kad sloboda nasmija se gradu,
Mrtvo tjelo predali smo hladu:
Crnoj zemlji ispod vitih jela,
Posred Lapca, posred grada b’jela.
Plače gora, plače svaka grana:
Nema nama Matića Stojana.
Omladino, ti ćeš vjerna biti,
Matićevu glavu osvetiti.
Izdajice, jađena vam nana —
Platićete Matića Stojana!
</pre>

<p>(Savo Orović, ratni dnevnik 1941-1945 (str. 278), Beograd 1972.)</p>
