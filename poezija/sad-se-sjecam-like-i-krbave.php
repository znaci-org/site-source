<p>(<i>narodna pjesma</i>)</p>

Evo prođe dvaest godin’ dana,<br/>
Čini mi se kao da sam sanja’;<br/>
Tad sam bio u najljepšem cvijetu,<br/>
Ne znah mlađan da živim na svijetu.<br/>
Sad se sjećam Like i Krbave,<br/>
Pamtim borbe i klance krvave;<br/>
Hodio sam kroz Kapelu pješke,<br/>
Pamtim borbe krvave i teške.<br/>
Sanjam rijeke, gore i livare;<br/>
I heroje pokraj sebe mlade,<br/>
Što j’ junački Kordun odgojio,<br/>
Teško j’ reći koj’ je bolji bio.<br/>
Sve po izbor, istina je znana,<br/>
Sto junaka, hrabrih partizana;<br/>
U tu četu hrabrije brđana<br/>
I mene je otpremila nana.<br/>
Kakvih, druže, tu bješe junaka,<br/>
Od Vojnića pa do Međeđaka,<br/>
Pa i selo Bukovica cijela<br/>
Rodi borce za velika djela.<br/>
Sve je srpska porodila majka<br/>
I ustaška osudila hajka.<br/>
Te brđane volio je svako,<br/>
Pa ih nije zaboravit’ lako.<br/>
Kad se sjetim čete komandira,<br/>
Bol za njime u srce me dira:<br/>
Junak Mićo Gaćeša je bio,<br/>
Smjelo četu u boj je vodio.<br/>
Rodom bješe iz Gaćeša sela,<br/>
Malog sela kraj Utinje Vrela.<br/>
Malo selo, al’ puno junaka,<br/>
Gorostaea, kršnije gorštaka.<br/>
Zjača Glišo bješe junak pravi:<br/>
Na jurišu kuda nagaravi.<br/>
Tu poteku potoci krvavi<br/>
Jer taj ne zna da se zaustavi.<br/>
Kad se sjetim Mrkonjić Ilije:<br/>
Takvog borca na daleko nije,<br/>
Koji nije mnogo govorio,<br/>
Al’ se uvijek junački borio;<br/>
Mrka oka a crnoga brka,<br/>
K’o da gledam Mandušića Vuka;<br/>
Visok bješe a tanak u pasu,<br/>
Mio bješe a junak na glasu;<br/>
S njim u četi dva njegova sina,<br/>
Sve junaci, gledati j’ milina.<br/>
Kad se sjetim Šimulije Zora,<br/>
Kršna momka k’o Petrova gora;<br/>
Žuta brka a muškije vjeđa,<br/>
Taj u boju ne poznaje međa;<br/>
Kao jelen koraka je laka,<br/>
Nikad džabe ne troši metaka,<br/>
Đe pogleda pogoditi mora;<br/>
Od glasa mu odjekuje gora.<br/>
Do njega je junak, ljuta zmija,<br/>
Stojaković mlađani Ilija;<br/>
A do njega Radojičić Marko,<br/>
Voljesmo ga kao sunce žarko;<br/>
Uvijek vedar, duša čete bio,<br/>
A o smrti nije ni mislio.<br/>
Kad se sjetim Pavlovića Mane<br/>
Kog rodiše iste ove strane;<br/>
Tih i šutljiv k’o Radonja rijeka,<br/>
Al’ u boju ne znade prepreka;<br/>
Pa Miloša Mrkonjića — Brke,<br/>
Što dušmanu taj zadade trke!<br/>
Kad se sjetim Živka Petrovića,<br/>
Muške snage, pravog Obilića;<br/>
Izrastao kao vita breza,<br/>
Sam pograbi teškog mitraljeza,<br/>
Sam ga nosi sa kote na kotu<br/>
Da dušmanu zadaje strahotu;<br/>
On je dika bataljona bio,<br/>
Junački je smrti prkosio.<br/>
Još se sjećam Bižića Nikole,<br/>
Takve, braćo, ne bijaše lole;<br/>
Lijepo momče kao vita jela,<br/>
Bujne kose a pogleda smjela;<br/>
Za njim mnoga uzdahnula cura,<br/>
Al’ u boju bješe kao bura<br/>
Što pred sobom obara drveće,<br/>
Tako bješe kud se Niko kreće.<br/>
Sve ih sanjam jer sam s njima bio,<br/>
Al’ ko b’ cijelu četu izbrojio<br/>
Jer u četi stotina ih bješe,<br/>
Sada mrtvi u snu mi se smješe.<br/>
Sanjam četu, ide u koloni,<br/>
Kao živi preda mnom su oni,<br/>
Ali iz sna kad se duša prene,<br/>
Teška tuga tad obrva mene.<br/>
Pusta želja da san bude java:<br/>
Strašna zbilja jade mi zadava.<br/>
Sanjam vodu, to su muke teške:<br/>
Na vrućini kroz Kapelu pješke,<br/>
Sunce grije, a vrućina bije,<br/>
Kapi vode u Kapeli nije;<br/>
Kapela se digla pod oblake,<br/>
Ali bješe muka za junake.<br/>
Tada ljetno pojavi se jutro,<br/>
Čeka dušman, i trag mu se utro.<br/>
Opet borba razgori se ljuta,<br/>
Trešti rafal što junake guta.<br/>
Zato pišem da bi mlađi znali<br/>
Za junake kako su padali,<br/>
Da se znade tužna priča ova,<br/>
Što nas košta čupanje korova,<br/>
Što nas košta željena sloboda<br/>
I obrana zemlje i naroda<br/>
Od prokletih fašističkih pasa<br/>
K’o koljači što stekoše glasa.<br/>
Još se sjećam k’o da bješe juče;<br/>
Talijanska haubica tuče<br/>
I granata pogodi junaka,<br/>
Boj s’ nastavlja, al’ zaplaka majka,<br/>
Boj se bije, gledam Jezerane,<br/>
Pade Mićo, pokosi ga tane,<br/>
Al’ njegovo srce u nas bije,<br/>
Pa nas dušman pobjedio nije.<br/>
I Vaganac gledam kao sada,<br/>
I tu Prva bijaše brigada.<br/>
Tu stotina drugova nam pade,<br/>
Al’ i dušman tu doživi jade.<br/>
Ali ko bi bitke izbrojio<br/>
I u boju svakom svjedok bio,<br/>
Kud su borci kosti posijali,<br/>
Za slobodu živote davali.<br/>
Ovo piše Grijaković Vlade,<br/>
Borac Prve udarne brigade.<br/>
Piše ono što upamti jako,<br/>
Moju četu sad prebrojim lako.<br/>
Živih osta desetina sada,<br/>
Pa se ni to ne skupi nikada.<br/>
A od tuge ja i pjesmu skratim,<br/>
Mrtve pjesmom ne možem da vratim,<br/>
Ali dajem što vam mogu dati<br/>
Kad u prošlost sanak me povrati<br/>
I sjeti me drugova junaka —<br/>
Neka im je crna zemlja laka.<br/>
I poruka od ratnoga druga,<br/>
Da i meni odumine tuga;<br/>
Tito nas je dov’o do pobjede,<br/>
Fašisti vam na grobu ne sjede,<br/>
Vaša djeca novi život grade<br/>
I u njima vi živite sade!<br/>

