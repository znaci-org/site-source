<h1>Početak ustanka na Kordunu</h1>

<p>(<i>narodna pjesma</i>)</p>

Crne knjige istorija piše:<br />
Hitlerovci zemlju pregaziše,<br />
S lukavstvom su narod zavadili<br />
I cijeli Balkan zauzeli.<br />
Poštene su vješali i klali,<br />
Pokvarene ljude kupovali:<br />
U Srbiji Dražu i Nedića,<br />
U Hrvatskoj Antu Pavelića<br />
I sa njima dosta pomagača,<br />
Da je banda razbojnička jača.<br />
Javio se neki dobre volje<br />
Da nam narod ubija i kolje.<br />
Najprvi je Paveliću Ante,<br />
Okupio bivše emigrante;<br />
I odmah je sastavio vladu<br />
U Zagrebu u bijelome gradu.<br />
Odluku su crnu donijeli.<br />
Par hiljada skupiše krvnika,<br />
Pljačkaroša — crnih razbojnika.<br />
Kad su ovu bandu okupili,<br />
Svi u službu fašistima stali,<br />
Na junački Kordun udarili,<br />
Srpski narod pljačkali i klali:<br />
Od Veljuna i mjesta Krnjaka,<br />
Od Vojnića i Vrgina Mosta<br />
Srba ljudi poklali su dosta.<br />
U šume je narod pobjegao,<br />
U obranu života je stao<br />
Uz svog sina novog Obilića —<br />
Hrabrog vođu Stanka Opačića.<br />
Junak Ćane ustanak priprema,<br />
A oružja jako malo ima.<br />
Bez obzira on ustanak diže,<br />
Dok i Veco iz Karlovca stiže.<br />
S Ćanicom se zajednički bori,<br />
To bijaše u Babinoj gori.<br />
Još je četa bez oružja bila,<br />
Dušmanska je na nji’ pošla sila.<br />
Košarić je u Debeloj kosi,<br />
Koja ime glasovito nosi,<br />
Sa Ćanicom društvo sastavio,<br />
Partizanski odred pojačao.<br />
Još je dosta druga prilazilo,<br />
Al’ oružja nije se imalo.<br />
Bez oružja ratovanja nema,<br />
Drug se Veco u Karlovac sprema,<br />
Drug Nikola iz Tušilovića<br />
I sa njima Opačić Dragica,<br />
Opačića Stanka drugarica.<br />
U Karlovac potjeraše kola,<br />
Sprovodnik je Basara Nikola.<br />
Devet pušak’ u kola staviše,<br />
Po puškama daske postaviše<br />
I zdravi se kući povratiše.<br />
Družina se osjetila jaka<br />
Kad je devet dobila pušaka.<br />
Odmah društvo hrabroga Ćanice<br />
Telefonske potrgalo žice,<br />
Pokraj ceste zasjedu staviše,<br />
Tu ustašu Starića ubiše<br />
I bandita nekog Mihelčića.<br />
Na brzinu braća priskočiše,<br />
Karabinke dvije ugrabiše,<br />
Dva pištolja, stotinu metaka.<br />
Sa tim, braćo, započeše borbu<br />
I ustanak dižu za slobodu.<br />

<p>(Narodne pjesme Korduna, sakupio i uredio Stanko Opačić-Ćanica, Zagreb: Prosvjeta, 1971, str. 304-305.)</p>