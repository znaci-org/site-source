<h1>Desanka Maksimović - Spomen na ustanak</h1>

Srbija je velika tajna:<br/>
ne zna dan šta noć kuva,<br/>
niti noć šta zora rađa,<br/>
ne zna grm šta susedni grm sanja<br/>
niti ptica šta se događa<br/>
između granja.<br/>
<br/>
Ne zna gušter šta puzi ispod kamenja,<br/>
niti kukuruza struk sluti<br/>
šta se u susednoj njivi sprema.<br/>
Svakoga časa sve se menja,<br/>
nijednog kuta ni lista nema<br/>
da nije tajna.<br/>
<br/>
Ko zna šta krije u sebi<br/>
i ta nevina rosa sjajna:<br/>
ti seljački poslovni krici<br/>
što se s brda na brdo čuju<br/>
zaveru možda kuju.<br/>
<br/>
Ko će u toj zemlji kada<br/>
znati šta i devojka mlada<br/>
u nedrima netaknutim nosi;<br/>
kakvu tešku tajnu<br/>
u rukama svojim drži dete;<br/>
i starica pogrbljena svaka<br/>
do kakve se uputila mete.<br/>
<br/>
U toj zemlji i vetri,<br/>
i mirisi, potoci i reke,<br/>
i crkvena zvona<br/>
potajno pronose vesti,<br/>
na prvom zavijutku,<br/>
gde šuma počinje ona,<br/>
ko zna šta možeš sresti.<br/>
<br/>
U toj zemlji ni zečjoj stopi<br/>
neprijatelj verovati ne sme,<br/>
ni tragu konjskih kopita.<br/>
Dogovori su možda tajni<br/>
i žetelačke pesme<br/>
