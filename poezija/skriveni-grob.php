<h1>Janus Međedović - Skriveni grob</h1>

<i>Vaku Đuroviću</i>

U zoru je pao.<br/>
Na nosila tugu svaka staza lije.<br/>
I kamen bi stanac za njim zaplakao,<br/>
a on ni u smrti glas pustio nije.<br/>
<br/>
Ko bor se survao.<br/>
I gora ga jekom svojim žali<br/>
i uzdasi teku niz kolonu,<br/>
suza bratska bratsko oko zali<br/>
i planinu Zelengoru tronu.<br/>
<br/>
I nestade Vaka.<br/>
Raka bez znaka,<br/>
kao ćivot sveti<br/>
čuva ga u hladu<br/>
i moli jelu mladu,<br/>
da mu pjeva o nama,<br/>
o našim bitkama,<br/>
što će nad njegovim grobom proletjeti!<br/>
<br/>
Ko bor se survao!<br/>
Ali grana s nama, s listanjem barjaka,<br/>
s našim bolom što se mržnjom razmahao,<br/>
da ljubav izraste kao vječnost jaka!<br/>
<br/>
1943.<br/>
<br/>
(Iz knjige Jugoslavenko pjesništvo NOB i revolucije, odeljak SR Crna Gora)
