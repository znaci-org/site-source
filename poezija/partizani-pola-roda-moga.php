<h1>Partizani, pola roda moga</h1>

<p>(<i>narodna pjesma</i>)</p>

Partizani, pola roda moga,<br>
Tamo imam brata i dragoga.<br>
<br>
Nema cvijeća što su jorgovani,<br>
Ni boraca što su partizani.<br>
<br>
Imam brata, mladog partizana,<br>
On je borac od prvoga dana.<br>
<br>
Imam dragog zajedno sa bratom,<br>
Otišli su zajedno sa ratom.<br>
<br>
Partizani pola roda moga,<br>
Vratite mi brata i dragoga.

<h2>II verzija</h2>

Nema cveća što su jorgovani,<br />
ni boraca što su partizani.<br />
<br />
Partizani – pola roda moga,<br />
tamo imam brata i dragoga.<br />
<br />
Cveta cveće, cveta ruža rana,<br />
pobeda je naših partizana.<br />

<h2>III verzija</h2>

Partizani pola roda moga <br />
tamo imam brata i dragoga <br />
<br />
imam brata imam i dragana <br />
obadva su dođe partizana.

<p>(iz Miroslav Špiler, Partizanske narodne pjesme iz Like, 1941- 1943.)</p>

<h2>IV verzija (Alaj volim u kolu igrati)</h2>

Alaj volim u kolu igrati,<br/>
Kad se sa mnom partizan uhvati.<br/>
<br/>
I ja imam brata partizana,<br/>
On je borac od prvoga dana.<br/>
<br/>
Ni moj dragi nije više prelac<br/>
Već u četi puškomitraljezac.<br/>
<br/>
Partizani pola roda moga<br/>
Tamo imam brata i dragoga.<br/>
<br/>
Ja ne volim ko' partizan nije,<br/>
Koji pušku primiti ne smije.<br/>
<br/>
Što valjalo pušku pripasalo,<br/>
Samo smeće oko kuće šeće.<br/>

<p>(Dušan Baić, Kotar Vrginmost u NOB, Beograd 1980.)</p>