    function showByAuthors() {
        const dict = [...document.querySelectorAll('h4')]
            .reduce((acc, heading) => {
                const {
                    innerText: key,
                    nextElementSibling: next
                } = heading
                const list = next.nodeName == 'IMG' ?
                    next.outerHTML + next.nextElementSibling.innerHTML :
                    next.innerHTML
                return {
                    ...acc,
                    [key]: key in acc ? (acc[key] + list) : list
                }
            }, {})

        const html = Object.entries(dict)
            .filter(([name]) => name != 'Anonimne / narodne pesme')
            .sort((a, b) => a[0].localeCompare(b[0]))
            .map(([name, list]) => (`<article><h4>${name}</h4><ul>${list}</ul></article>`))
            .join('')

        document.querySelector('main').innerHTML = `
            <button onClick="window.location.reload()">Vrati podrazumevano</button>
            <div class="knjige columns">${html}</div>
        `
    }

    document.querySelector('button').addEventListener('click', showByAuthors)
